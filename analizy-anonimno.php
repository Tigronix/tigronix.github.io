<h2>Что необходимо знать при сдаче крови</h2>

<p>Важно знать, что очень часто анализы крови, а если точнее забор крови проводят утром. Это время является оптимальным для пациента, так как точный анализ может зависеть от того, взята кровь натощак или же после приема пищи. Следует отметить, что период времени между сдачей крови и последним приемом пищи должен составлять от 8 до 12 часов.</p>

<p>Необходимо прекратить прием любых лекарственных препаратов за две недели до сдачи анализов. В период до нескольких суток не принимайте никакие алкогольные напитки или жирную пищу.</p>

<p>Если во время, а точнее за несколько часов до сдачи анализов вам захотелось пить, выпейте обычной воды, не стоит пить соки или любые другие напитки, в состав которых входит сахар. Если вы пришли на сдачу анализов анонимно, вам следует успокоиться и расслабится. Не нагружайте себя физическими нагрузками и старайтесь не курить. В том случае, когда у вас в тот же день назначены еще какие-либо процедуры, запомните, что после УЗД, рентгеновского исследования или физиотерапии, забор крови противопоказан.</p>

	        	
		
