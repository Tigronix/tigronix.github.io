<p>Но к вирусным половым инфекциям относятся еще и вирус папилломы, а также всем знакомый герпес.</p>

<p>Все они попадают в раздел специфических заболеваний. Однако, есть и такие которые туда не попадают, а возбудителями являются кишечник и кожный покров. Проходя в клинике необходимый осмотр и сдав анализы, можно понять, к какому именно виду относится то или иное заболевание.</p>

<p><img style="width: 350px; height: 226px; margin: 10px; float: left;" src="../img/3.jpg" alt="анализы на инфекции у женщин" /><img style="width: 350px; height: 226px; margin: 10px; float: left;" src="../img/analizi-na-zppp.jpg" alt="анализы на инфекции у женщин" /></p>

<p>Необходимо знать, что бывают заболевания, называющиеся вульвовагитные. Это когда протекание болезни отмечается воспалением влагалища и наружные половые органы, шейка матки. Также бывают и заболевания, когда происходит воспаление малого таза. Например, при эндормитах происходит воспаление слизистой оболочки матки, при сальпингитах – трубы, при сальпингоофоритах – трубы яичников, при эндормиометритах – слизистая и мышечная оболочка матки.</p>

<p>Воспаление всей матки называется панметритом. И если лечение не начать вовремя, то это заболевание может привести к очень серьезным последствиям. Может развиться тазовый перетонит, когда воспаление поражает всю брюшину.</p>

<p>Каждое из этих заболеваний отличается по симптоматику, и очень важно, чтобы диагностику заболевания проводил именно специалист.</p>

<p>Проводя анализы инфекционных заболеваний, проводят такие исследования;</p>

<ul class="list">
	<li><span class="bold"> мазок на флору и цитологию (его берут из влагалища);</span></li>
	<li><span class="bold"> анализ на выявление половых инфекций;</span></li>
	<li><span class="bold"> TORCH анализ;</span></li>
	<li><span class="bold"> выявление ВИЧ-инфекции</span></li>
</ul>

<p>Обращаться в нашу клинику в Москве желательно при любых выделений из влагалища. В частности если Вы заметили, что они имеют резкий запах или неприятный цвет. Своевременное прохождение исследований поможет вовремя выявить молочницу, бактериальный вагиноз, трихомониаз.</p>

<p>Иногда при помощи анализов можно определить аллергическое раздражение, и избавится от проблемы. У женщин в возрасте бывают такие заболевания как атрофические вагиниты. Это возникает вследствие дефицита гормонов.</p>

<p>Также выделения могут быть спровоцированы воспалением шейки матки, органов малого таза. Чаще всего это первопричина проникновения в организм гонококков или хламидий.</p>

<p>Беременные женщины сдают TORCH анализ. Он делается для выявления таких заболеваний, которые передаются ребенку от матери. Например: токсоплазмоз, краснуха, цитомегаловирус, герпес.</p>

<p>Помните, что своевременное обнаружение заболевания поможет правильно и без последствий вылечить его!</p>

	        	
		
