<h2>Мужчина и папилломатозная инфекция</h2>

<p>Папиллома, а точнее ее вирус, может встретиться не только у женщины, но и у мужчины. Такая инфекция часто встречает тех представителей мужского пола, которые ведут незащищенную половую жизнь, а еще чаще она встречается в сопровождении других заболеваний, которые передаются половым путем (далее ЗППП). Существует статистика, которая утверждает, что 90.1% мужчин, которые сдавали анализы на вирус папилломы человека (далее ВПЧ) и лечились по поводу ЗППП, имеют в крови ВПЧ.</p>

