<h2>Признаки инфекций:</h2>

<ul class="list">
	<li><span class="bold">Боли в гениталиях</li>
	<li><span class="bold">Боль в простате у мужчин</li>
	<li><span class="bold">Боли при мочеиспускании</li>
	<li><span class="bold">Гипертермия</li>
	<li><span class="bold">Жжение </li>
	<li><span class="bold">Различные выделения из наружных путей</li>
</ul>

<p>Биохимические лаборатории, которые обладают новейшим оборудованием для проведения исследования путем полимеразной цепной реакции. Анализы на ЗППП, таким методом позволяют брать выделения из мочеиспускательного канала, влагалища или шейки матки на анализы, которые будут готовы в течение нескольких суток.</p>

<p><img alt="анализы на половые инфекции" src="../img/in5c3xcu50l.jpg" style="width: 350px; height: 220px; margin: 10px; float: left;"><img alt="анализы на половые инфекции" src="../img/b8306_328592.jpg" style="width: 350px; height: 220px; margin: 10px; float: left;"></p>

<p>Исследования на ЗППП можно провести даже при отправке удаленных родинок или кондилом. Такое исследование поможет диагностировать и найти любые инфекционные возбудители, которые находятся в организме человека и которые передаются в случае незащищенного контакта.</p>

<p>Исследование на ЗППП, проводится за счет выделения возбудителя. Проводится посев выделений из уретры на питательную среду. Результат будет готов в течении от 3 до 7 дней. Длительность обнаружения зависит от вида возбудителя.</p>

<p>Основным достоинством такого обследования является то, что при получении результатов можно провести пробу на антибиотики, после чего подобрать самое лучшую схему лечения, которая избавит от инфекций. &nbsp;</p>

<p>Анализ на ЗППП, может потребовать включить реакцию ИФА, которая поможет понять, как долго проводилось ношение возбудителя, без проявления симптомов. Такие анализы используют для диагностирования возбудителей хламидиоза, цитомегаловируса или герпеса.</p>

<p>Антитела к ВИЧ не говорят о наличии СПИДа, но дают возможность установить наличие вируса в организме и установить факт заражения.</p>

<p>Ложный результат может быть в том случае, когда у человека наблюдается какое-либо аутоиммунное заболевание, например виру Эпштейна-Бара, а также при наличии антител к равматоидному артриту. &nbsp;</p>

<p>Также ложные результаты могут наблюдаться в том случае, когда проходит терминальная стадия СПИДа, а также тогда, когда прошло небольшое количество времени после контакта с носителем. В том случае, если есть подозрения на заражение ВИЧ, процедуру необходимо провести повторно по истечению 2-3 месяцев. &nbsp;&nbsp;&nbsp;</p>

<p><span class="bold">RW</span> – это исследование крови, которое поможет диагностировать сифилис. Возбудитель сифилиса беспрерывно проводит губительное воздействие на организм, из-за чего болезнь очень быстро приводит к летальному исходу. Заразиться данной болезнью можно в том случае, если был близкий контакт с человеком, у которого наблюдается вторичная стадия заболевания. Случиться такое может при связи с больным или в том случае, когда были использованы гигиенические средства зараженного человека. Также может быть передача болезни в момент беременности, от матери к ребенку, но лишь тогда, когда женщина болеет сифилисом.</p>

		
