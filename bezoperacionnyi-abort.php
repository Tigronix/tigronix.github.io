<p><span class="bold">Медикоментозный аборт без предварительной записи в Москве</span> - Вас могут принять на процедуру безоперационного прерывания беременности только в порядке общей очереди, если имеются свободные места.</p>

<p><span class="bold">Эффективность безоперационной процедуры обратно пропорциональна сроку беременности</span></p>

<p><img style="width: 618px; height: 144px; margin: 10px;text-align: center;" src="../img/877gg.jpg" alt="безоперационный аборт" /></p>

<div class="clr"></div>

<p><span class="bold">Последовательность действий при проведении безоперационного прерывания беременности следующая:</span></p>

<p>В день обращения к врачу, проводится необходимое медицинское обследование: проводится УЗИ для определения срока беременности, обследование (анализ крови на гепатиты, сифилис, ВИЧ-инфекцию, группу крови и резус-фактор; мазок из влагалища на наличие инфекций). После этого нужно согласие и Ваше подтверждение на фамакологический аборт. В присутствии врача, в случае отсутствия у вас противопоказаний,  вы принимаете таблетки и возвращаетесь домой (данные таблетки прекращают доступ питательных веществ к плодному яйцу и оно погибает). В ближайшие часы у Вас могут начаться незначительные кровянистые выделения из половых путей. Ваш следующий визит – через 36-48 часов. Врач даёт Вам принять таблетки простагландина (под его действием матка сокращается и плодное яйцо выходит из полости матки) и в течение 2-4 часов Вы находитесь под  наблюдением. На этом этапе кровянистые выделения становятся более интенсивными и напоминают обильную менструацию.  Плодное яйцо выходит, как правило, в этот или в один из последующих дней. Ваш следующий визит – через 7-10 дней  Вы проходите контрольное УЗ исследование на предмет остатков плодного яйца и сокращения матки, а также на консультации с врачом обсуждаете тему контрацепции. </p>

<p>Во время безоперационного прерывания беременности не требуется сохранять постельный режим.</p>



	        	
		
