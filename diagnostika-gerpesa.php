<p><span class="bold">«Клиника на Авиамоторной» поможет вовремя выявить герпес</span></p>

<p>Составляющие комплексной проверки</p>

<p>Для проведения любых методов лабораторного исследования у пациента осуществляется забор биологического материала (мазок, соскоб, жидкости и секреты организма, содержимое везикул и др.).</p>

<p><span class="bold">Вирусологические методы выявления герпеса.</span> Диагностика герпеса включает анализ цитопатических изменений в зараженной вирусом клеточной культуре методом электронной или световой микроскопии, реакцию нейтрализации герпеса с использованием стандартных антисывороток (проводится на экспериментальных животных); идентификацию антигенов ВПГ при помощи методов иммуноферментного анализа и иммунофлюоресценции. Эффективность выявления герпеса вирусологическими методами составляет от 90 до 95%.</p>

<p><span class="bold">Цитоморфологические методы выявления герпеса.</span> С помощью этих видов диагностики герпеса можно обнаружить морфологические изменения в клетках органов и тканей. Существует две подгруппы цитоморфологических методов: электронная и световая микроскопия. С их помощью выявляются характерные изменения: некроз, гиперхромные ядра клеток, многоядерные клетки с включениями и др. Эффективность выявления вируса простого герпеса составляет до 70%.</p>

<p><span class="bold">Молекулярно-генетическая диагностика герпеса.</span> Полимеразная цепная реакция – новый вид лабораторных исследований, основанный на многократном воспроизведении определённого участка ДНК, который осуществляется в искусственных условиях с помощью ферментов. Копирование образца ДНК происходит только в том случае, если он присутствует в биоматериале и соответствует заданным параметрам. Эффективность выявления генитального герпеса с помощью ПЦР составляет 95%.</p>

<p><span class="bold">Выявление антигенов.</span> Для выявления антигенов герпеса используются серотологические методы: РСК (реакция связывания комплимента), иммуноферментый анализ, РПГА (реакция пассивной гемагглютинации), реакция нейтрализации. В основе всех перечисленных видов лабораторных исследований лежит специфическая реакция связывания антиген-антитело. Эффективность выявления герпеса с помощью серотологических методов составляет 50-70% при острой форме генитального герпеса и 30-45% – при хронической.</p>

<p><span class="bold">Регистрация иммунного ответа к вирусу простого герпеса.</span> В процессе исследования выявляются специфические антигены к заболеванию с помощью серотологических методов высокой чувствительности, циркулирующие в секретах и биологических жидкостях пациента, оценивается клеточный иммунитет к вирусной инфекции, делаются кожные пробы. Эффективность выявления герпеса отличается вариабельностью.</p>

<h2>Осложнения генитального герпеса</h2>

<p>При отсутствии лечения заболевания, герпес может вызывать поражение наружных женских и мужских половых органов: их слизистые оболочки пересыхают и покрываются кровоточащими трещинами. Раздражение может появляться в результате механического воздействия: при ношении тесного белья, половом акте. У пациентов, страдающих хронической формой заболевания, может развиться болевой синдром, что вызывает снижение либидо. Заболевание ведет к увеличению риска самопроизвольных абортов и бесплодия. У зараженных в процессе родов детей в течение первых часов жизни проявляются поражения глаз, кожи и головного мозга.</p>

