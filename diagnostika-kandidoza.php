<p><span class="bold">В «Клинике на Авиамоторной» Вы можете пройти комплексную процедуру диагностики на кандидоз и получить профессиональные медицинские услуги</span></p>

<p style="text-align: justify;">Виды диагностических исследований кандидозов</p>

<p><span class="bold">Микроскопические исследования.</span> Данный вид исследований является наиболее доступным. Материалом для проведения анализа является характерный для заболевания (кандидоз) крошковатый налет и белесые пленки, образующиеся на различных участках слизистой оболочки и кожи мочеполовых органов. Вначале проводят микроскопию при малом увеличении, которое позволяет обнаружить псевдомицелий (ложный мицелий грибов) и скопление дрожжевых грибов. Далее переходят на большее увеличение, при котором можно увидеть отдельные клетки грибов, их расположение и форму.</p>

<p><span class="bold">Культуральная диагностика.</span> Не всегда обнаружение грибов в исследуемом материале имеет абсолютное значение. Культуральная диагностика позволяет провести необходимую оценку по качественным и количественным показателям (по обсемененности, биохимическим морфологическим признакам). Для получения культур Candida используют жидкие и питательные среды с углеводами. Полученные культуры идентифицируются после изучения морфологии бактериальных клеток и внешнего вида выросшей колонии.</p>

<p><span class="bold">Серологические реакции.</span> При исследовании на кандидоз применяют реакцию пассивной гемагглютинации (РПГА), реакцию преципитации (РП), реакцию связывания комплемента (РСК), реакцию агглюцинации (РА). Результат является положительным при увеличении титра соответствующих антител в 4 и более раз. Для большей надежности исследование на кандидоз проводят 2-3 реакции.</p>

<p><span class="bold">Полимеразная цепная реакция.</span> Наиболее объективным среди молекулярно-биологических методов признана полимеразная цепная реакция (ПЦР). Для проведения исследования используется пара искусственно синтезированных олигонуклеотидов, которая повторяет строение специфического участка гена микроорганизма, подвергающегося идентификации. Чаще всего ПЦР применяется для диагностики диссеминированных и глубоких форм заболевания (кандидоз), то есть анализ проводится совместно с исследованием крови, биоптатов глубоких тканей, стерильных сред организма.</p>

<p><span class="bold">Иммуноферментный анализ.</span> Иммуноферментный анализ (ИФА) применяется для определения наличия антител к антигенам Candida albicans в сыворотке крови пациента. Как правило, ИФА используется с учетом клинической симптоматики заболевания и в комплексе с результатами культурального исследования. Иммуноферментный анализ позволяет провести распознавание смешанных инфекций, которые вызваны бактериями и грибами рода Candida (кандидоз). Также ИФА проводят с целью оценки эффективности лечения заболевания кандидоз.</p>

<p><span class="bold">Иммуноферментный анализ.</span> Такой анализ позволяет обнаружить и измерить количество специфических антигенов в плазме крови пациента. В процессе иммуноферментного исследования обращается внимание на клеточный и гуморальный компоненты иммунитета. Анализ позволяет получить информацию о качестве и количестве защитных клеток крови (лимфоцитов), которые обеспечивают противовирусный иммунитете. Также исследование дает представление о наличии в крови иммуноглобулинов и иных защитных белков.</p>

<p><span class="bold">Проведение своевременной и точной диагностики поможет назначить эффективное лечение и избежать серьезных осложнений заболевания!</span></p>

