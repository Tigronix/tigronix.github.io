<p>Современная медицина использует достаточно эффективные методы диагностики хламидиоза.</p>

<p>Конечно, достоверный результат дает только комплексное использование нескольких диагностических методик. И у каждой есть свои плюсы и минусы. Одни методы предоставляют объективную информацию, но являются сложными в использовании и применении. Другие диагностики позволяют быстро провести скрининг большой массы населения, но при этом нуждаются в уточнении и перепроверке. Благодаря развитию автоматизированных диагностических аппаратов появилась возможность проводить сложные тесты, основанные на анализе ДНК или РНК, но при этом повысились требования к качеству материала и реагентов, а также к подготовке лаборантов и четкости технологии проведения анализа.</p>

<p>При проведении лабораторных исследований проводится изучение цитоплазматических включений в биоматериале. Цитоплазматические включения хламидий (тельца Гальберштедтера - Провачека) – это обособленные массы, отличные от состава основной клетки по цвету и внутренней структуре. Они содержат как крупные ретикулярные тельца возбудителя, так и мелкие, элементарные.</p>

<p><span class="bold">Культуральный метод</span></p>

<p>Наиболее сложным, но при этом одним из самых информативных считается традиционный культуральный метод, так называемый «золотой стандарт» для выделения С. trachomatis способом изоляции возбудителя на культуре клеток. Целью метода является выявление специфических цитоплазматических включений. На проведение данной диагностики требуется от двух до трех суток и наличие специально оборудованной лаборатории. Клинический материал обрабатывается различными растворами, при этом пробирки подвергаются воздействию температур и интенсивному встряхиванию. Затем полученный материал исследуют с помощью светового микроскопа. Тельца хламидий окрашиваются в определенный цвет: элементарные тельца имеют розовый цвет, а ретикулярные – синий.</p>

<p><span class="bold">Микроскопический метод</span></p>

<p>С помощью световой и люминесцентной микроскопии происходит выявление хламидий в исследуемом материале (флуорохромированием и иммунофлуоресценцией). Необходимо наличие светового микроскопа с большим увеличением, приготовление препарата высокого качества, а главное – высококлассного бактериолога. Препараты окрашиваются по различным методам, но классическим считается методика Романовского-Гимзе. Цитоплазматические включения возбудителя инфекции, взаимодействуя с препаратом, приобретают темно-фиолетовый цвет.</p>

<p><span class="bold">Серологическая диагностика</span></p>

<p>Метод диагностики используется в случаях возникновения сложностей при взятии клинического материала, например у детей. Это очень простая в проведении методика. В основе серологической диагностики лежит выявление специфических антител, накопленных в сыворотке крови и секретах инфицированного организма при иммунном ответе на возбудителя инфекции. В зависимости от первичности или вторичности инфицирования накопление антител имеет различную концентрацию. Следует учитывать, что метод не подходит для выявления острых генитальных инфекций, но помогает определить хронические и осложненные заболевания. Необходимо проводить</p>

<p>дополнительные исследования и наблюдения, так как бывает большой процент ложноположительных реакций, при перенесенном в прошлом инфицировании. Однако, если необходимо обследовать большую группу людей, то серологическая диагностика будет самым простым и быстрым способом для определения лиц, которым нужно будет пройти дополнительное обследование.</p>

<p><span class="bold">Диагностика с помощью иммуноферментного анализа (ИФА)</span></p>

<p>Исследуемые пробы обрабатываются наборами реагентов. Клинический материал, имеющий в наличии хламидии при изучении спектрофотометром изменяет цвет. Если проба окрашивается в желто-оранжевый цвет, значит констатируется наличие инфекции. По интенсивности окраски можно говорить о количестве антигена хламидий. Этот метод, конечно же, прост в организации и проведении, но при этом высока вероятность ложноположительной реакции. Кроме того, стоимость аппаратуры остаточно высока, а проконтролировать качество исследования сложно.</p>

<p><span class="bold">Метод ДНК-зондов</span></p>

<p>Для выявления ДНК хламидий выделенную из образца общую ДНК денатурируют кипячением, наносят на нитроцеллюлозный фильтр и гибридизируют с биотинированным ДНК-зондом. Затем фильтр промывают, чтобы удалить не связавшийся зонд, отрабатывают специальными составами и субстратным раствором. Появление окрашенных цитоплазматических включений будет свидетельствовать о присутствии в пробе хламидиоза. Метод диагностирует от 78 до 98%. Но данная диагностика не показывает вялотекущую степень заболевания.</p>

<h2>Молекулярно-биологические методы (ПЦР и ЛЦР)</h2>

<p><span class="bold">Метод полимеразной цепной реакции (ПЦР)</span></p>

<p>Является одним из наиболее часто применяемых и точных способов определения уретрогенитальной хламидийной инфекции. Методика состоит из повторяющихся циклов нагрева и охлаждения реакции для денатурации и репликации ДНК ферментами, так называемое термоциклирование. Под воздействием реагентов и температур инфицированные участки ДНК даже при минимальной концентрации многократно копируются. Наличие специфического фрагмента определяется с помощью контрольной ДНК хламидии. Если в пробе исследуемого образца после проведения диагностики специфических ДНК-продуктов не обнаруживается, значит анализ отрицательный. Исследуются как цервикальные и уретральные мазки, так и осадок мочи. Диагностика проводится достаточно быстро, в полуавтоматическом режиме. Постой способ сбора проб. Высокая точность и объективность метода позволяет выявить бессимптомно протекающее заболевание.</p>

<p><span class="bold">Лигазная цепная реакция (ЛЦР)</span></p>

<p>В его основе лежит лигирование олигонуклеотидов, комплементарных определённой ДНК-мишени. Диагностика основана на способности ДНК-лигаз восстанавливать фосфодиэфирные связи в одноцепочечных разрывах двухцепочечных молекул ДНК. Реакционную смесь сначала прогревают, чтобы произошло лигирование, вследствие чего образуется фосфодиэфирная связь. При повышенной температуре этот гибрид плавится, и при понижении температуры олигонуклеотиды преимущественно гибридизуются с анализируемой ДНК. Изучение полученного материала указывает на получение или отсутствие продукта реакции, который может образоваться только если в образце присутствует «мутантный» олигонуклеотид. ЛЦР используется при проверке уретрогенитальных, эндоцерви-кальных образцов и в особенности эффективна для определения хламидиоза в моче у женщин. Надежность диагностики до 99%.</p>

	        	
		
