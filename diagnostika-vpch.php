<p>В случае обнаружения при диагностике доказательств наличия инфекции, может понадобиться биопсия, на основе материала которой будут проведены гистологические исследования.  Резко выраженный папилломатоз и акантоз эпителия (ВПЧ) можно легко обнаружить при диагностике на патоморфологию – в этом случае диагностика ВПЧ выявит, что лимфоциты и плазматические клетки в соединительной ткани инфильтрованы, а в клетках эпидермиса отмечается резкое увеличение вместимости митоз. Также диагностика ВПЧ покажет инфильтрацию в сосочковом и подсосочковом слое. Она, как правило, незначительна. В клеточных ядрах гранулярного слоя развивается койлоцитоз, в результате собираются вирионы и образуются сложные протеины, также заметны признаки экспрессии поздних генов. Койлоцыты – это большие окруженные кольцом из плотной амфофильной цитоплазмы клетки, имеющие пикнотические ядра и обширную область вакуолизации.</p>

<p>Также можно обнаружить капсидный радоспецифичный антиген при помощи иммунологических тестов диагностики. Тип вируса ВПЧ при этом определяется эидонуклиазным анализом ДНК, образец которого берется с инфицированных тканей, также в этом случае может быть применен метод  гибридизации с помощью нуклеиновых кислот.</p>

<h2>Вирус папилломы человека - ВПЧ. На чем базируются цитологические исследования</h2>

<p>Цитологическое исследование на ВПЧ базируется на методе окраски, когда определяется реакция клеточной структуры на кислые красители и основные пигменты. Такая диагностика ВПЧ была впервые проведена известным ученым Папаниколау, когда он применил метод окраски с целью выявления рака шейки матки и ВПЧ - вируса. Так в 1943 году диагностика ВПЧ была признана и ее назвали методом Папаниколау или сокращенно ПАП-тест – по первым буквам фамилии ученого.</p>

<p>ПАП-тест проводится в лабораторных условиях на основе материала, который забирается у пациента в ходе обычного гинекологического осмотра. Лаборатория, проводящая исследования, должна иметь весь набор необходимых реагентов,  в частности это гематокселин – основной краситель, и выступающий в роли кислого красителя зозин.  Цель диагностики – проследить реакцию ядер и клеточной цитоплазмы на введенный краситель.</p>

<p>Диагностика ВПЧ и ПАП-тесты проводят в несколько этапов, первый этап диагностики, безусловно, является сдача анализов. Следующий этап диагностики – дифференциальная диагностика, которая определит вид процесса: злокачественный или доброкачественный. Это довольно действующий метод, такая диагностика имеет точность 80% и более.Этот метод применяют в большинстве развитых стран для диагностики заболевания на ранней стадии развития. Всем женщинам старше 20 рекомендуется проводить исследования хотя бы раз в год.</p>

		
