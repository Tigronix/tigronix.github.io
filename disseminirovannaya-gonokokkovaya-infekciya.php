<p><span class="bold">Диссеминированная гонококковая инфекция (ДГИ)</span> отмечается в 0,2— 1,9% случаев инфекции слизистых. Хотя во многих регионах мира эта форма заболевания почти перестала встречаться, в то же время ДГИ продолжает оставаться серьёзной причиной заболеваемости и, в отдельных случаях, смертности в популяции молодых, сексуально активных лиц.<br />
<br />
 В США в начале 80-х годов ДГИ являлась наиболее распространённой причиной госпитализации по поводу инфекционного артрита, причем ежегодно регистрировалось 2,8 случаев заболевания на 100 000 населения. ДГИ продолжает оставаться значимым заболеванием в связи со стабильно высоким уровнем заболеваемости <a href="/lecheniye-gonorei/">гонореей</a> в развивающихся странах.<br />

</p>
