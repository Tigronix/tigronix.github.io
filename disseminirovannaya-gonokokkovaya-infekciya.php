<p>Остаётся неясной роль в развитии заболевания вторичных иммунодефицитов, обусловленных алкоголизмом, приёмом стероидных препаратов, внутривенным введением наркотиков и такими заболеваниями как системная красная волчанка и синдром недостаточности комплемента. Не исключено, что в отдельных случаях, эти факторы способствуют развитию ДГИ, особенно при рецидивирующих формах заболевания.<br />
<br />
 Патогенез ДГИ не всегда связан с непосредственной бактериальной инвазией в ткани. Определённую роль в патогенезе, вероятно, играют иммунные реакции, в частности, фиксация в тканях иммунных комплексов "антиген-антитело" и/или перекрёстная реакция между гонококковыми антигенами и тканевыми антигенами организма-хозяина. Иммунный механизм патогенеза ДГИ подтверждается обнаружением гонококковой ДНК в некоторых образцах, выделенных из суставов и не дающих бактериального роста при культивировании, а также присутствием в кожных высыпаниях и синовиальных оболочках иммунных комплексов при отсутствии роста гонококков. Эта гипотеза подтверждается также тем, что у отдельных пациентов с ДГИ выявляют такие аутоиммунные поражения кожи, как узловатая и многоформная эритема.<br />
<br />
 Системная инфекция, вызванная N. gonorrhoeae, обычно протекает в сравнительно лёгкой форме, причём температура тела редко повышается до 39°С. Наиболее распространённый симптом заболевания - ассиметричная или блуждающая полиартралгия, в 25% случаев отмечается моноартралгия.<br />
 Поскольку у большинства пациентов отсутствуют симптомы поражения урогенитального тракта в первичных очагах инфекции, они могут быть направлены к дерматологам или ревматологам.<br />
<br />
 Двумя другими классическими проявлениями инфекции являются <span class="bold">высыпания на коже</span> и <span class="bold">тендосиновит</span>. Примерно у 60% пациентов не удается обнаружить явных признаков синовиального выпота, причём более чем у 90% из этих лиц отмечается наличие кожных высыпаний и/или тендосиновита часто в сочетании с негнойным артритом.</p>

<p>Необходимо тщательно обследовать пациентов с целью выявления высыпаний, хотя только четверть из них обычно жалуется на поражения кожи. При осмотре обнаруживается папуловезикулёзная, везикуло-пустулёзная, геморрагически-некротическая сыпь.</p>

<p>Высыпания обычно локализуются ниже шеи, захватывая туловище, конечности, ладони, подошвы, и отсутствуют на волосистой части кожи головы, лице и во рту.</p>

<p>В отдельных случаях было описано появление узловатой эритемы, крапивницы, многоформной эритемы. Поражения кожи, как правило, исчезают через 4-5 дней, не оставляя рубцов.</p>

<p><span class="bold">Тендосиновит</span> обычно вызывает неприятные ощущения в руках и пальцах, но могут быть также поражены сухожилия в области мелких и крупных суставов нижних конечностей.</p>

<p>Дифференциальная диагностика гнойного и негнойного артрита может представлять трудности и часто требует проведения диагностической пункции поражённого сустава. У пациентов с гнойным выпотом в суставах число лейкоцитов в крови обычно составляет 30000-80000 клеток/мкл. Гнойный выпот в полость сустава почти никогда не приводит к долговременным нарушениям. Поэтому, хотя и может потребоваться повторная аспирация, внутрисуставное введение антибиотиков не рекомендуется. У этих больных поражения кожи или тендосиновит выявляются реже. Чаще всего поражается коленный сустав, реже - тазобедренный и суставы верхних конечностей.<br />
<br />
 Следует проводить дифференциальную диагностику ассиметричного артрита, обусловленного ДГИ, с инфекционным артритом, болезнью Рейтера и ревматической лихорадкой.</p>

<p>В отличие от ДГИ, которая чаще всего поражает молодых, сексуально активных женщин, <span class="bold">инфекционный артрит</span>, как правило, выявляется у пожилых, или очень молодых людей, или лиц с иммунодефицитами, причём преимущественно мужчин. Инфекционный артрит протекает как неблуждающий моноартрит, редко сопровождающийся кожными высыпаниями.<br />
<br />
 При <span class="bold">болезни Рейтера</span> артралгия также не имеет блуждающего характера, часто поражает нижние конечности, сочетается с конъюнктивитом и другими заболеваниями слизистых, не характерными для ДГИ.</p>

<p>В отличие от ДГИ болезнь Рейтера выявляется преимущественно у мужчин и, как правило, не сопровождается лихорадкой. Для этого заболевания характерны ассоциация с генотипом HLA B27 и наличием гиперкератоза.<br />
<br />
 При <span class="bold">ревматической лихорадке</span>, которая в развитых странах встречается крайне редко, отмечается высокая температура, более выраженный системный характер заболевания и типичная сыпь.</p>

<p>Поражения кожи при ДГИ могут быть сходны с таковыми при менингококковой септицемии, однако в последнем случае у пациентов обычно выявляют более острое системное заболевание с симптомами и признаками менингита.</p>

<p>Описанные в литературе гонококковые поражения мозговых оболочек, сердечных клапанов и костей встречаются редко.<br />
<br />
 <span class="bold">Дигностику ДГИ</span> можно осуществлять, основываясь на клинических признаках заболевания и положительных результатах культурального исследования мазков со слизистых. Количество лейкоцитов редко превышает 20 000 клеток/мкл, но СОЭ примерно в половине случаев превышает 50 мм/час. Достаточно часто выявляются анемия и повышенный уровень трансаминаз.</p>

<p>При культивировании проб крови N.gonorroeae обнаруживается в одной трети случаев, а при культивировании синовиальной жидкости пациентов с гнойным выпотом - примерно в половине случаев.<br />
 Таким образом, точные механизмы возникновения ДГИ при заражении N.gonorroeae, а также причины развития системной инфекции лишь у небольшого числа лиц до сих пор остаются неизвестными. Даже использование чувствительных методик не всегда обеспечивает бактериологическое подтверждение клинического диагноза.</p>

<p>В этих случаях может возникнуть необходимость в применении эмпирической терапии антибиотиками, воздействующими на устойчивые к пенициллину микроорганизмы. При дифференциальной диагностике артрита или гипертермии неизвестной этиологии у лиц молодого возраста следует учитывать возможность ДГИ.</p>

	        	
		
