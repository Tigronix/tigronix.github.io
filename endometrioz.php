<p>Есть несколько форм эндометриоза.</p>

<p>При генитальной форме эндометиоз охватывает органы малого таза, наружные половые органы, маточные трубы, влагалищную часть шейки матки. Одним словом органы женской репродуктивной системы.</p>

<p>Другая форма  называется экстрагенитальной. В этом случае эндометриоз распространяется на кишечник, легкие, мочеполовую систему, послеоперационные рубцы.</p>

<p>В зависимости от очага поражения эндометриоз по-разному проявляет себя. Это могут быть сильные боли во время менструации или полового акта. Может нарушатся менструальный цикл, выражается это в более обильных менструациях, появлению межменструальных кровяных выделений. В некоторых случаях заболевание эндометриозом может привести к бесплодию.</p>

<p>Причинами возникновения эндометриоза могут быть роды с осложнениями, кесарево сечение, аборт. Иногда толчком к развитию эндометриоза является диатермокоагуляция, использующаяся для лечения эрозии матки. Если все перечисленные вмешательства были произведены до начала менструации, то есть риск попадания клеток эндометрии в рану, где в последствии и начинает развиваться эндометриоз. Эндометрия – это ткань, которая во время менструации отторгается у каждой женщины.</p>

<p>Кроме того, симптомами этого заболевания могут быть и боли в области малого таза, мажущие выделения перед и после менструации. Немного реже у женщины могут возникать боль при дефекации, во время полового акта. Наиболее характерный симптом для эндометриоза – это кровяные выделения в межменструальный период. А боль появляется уже тогда, когда зараженные клетки прорастают в ткани.</p>

<p>При наличии таких симптомов врач назначает лапораскапию, которая показывает степень болезни и пораженные участки. УЗИ в таких случая назначают только при определении кисты в яичниках.</p>

<p>Метод лечения в таких случаях может быть гормональным на начальной стадии, хирургический и, наиболее реже, комбинированный. Комбинированный тип лечения совмещает в себе хирургическое вмешательство и гормональные препараты. Только врач может диагностировать и назначать лечение. Заниматься этим в домашних условиях или сомнительными способами  сомнительных врачей ни в коем случае нельзя.</p>

<p>Существует и профилактика этого заболевания. Это использование противозачаточных средств дабы предупредить нежелательную беременность, своевременное лечение гинекологических заболеваний, предупреждение родовых травм. Не стоит запускать воспалительные заболевания половых органов и гормональных нарушений, чтобы не спровоцировать эндометриоз.</p>

<p>Меньше всего рискуют заболеть эндометриозом женщины, злоупотребляющие курением, также те, кто долгое время принимают противозачаточные таблетки, в качестве противозачаточного средства используют спираль.</p>

	        	
		
