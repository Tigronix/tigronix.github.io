	        		<h1>Эндометрит</h1>
					
					<p>ЭНДОМЕТРИТ  – это воспаление слизистой оболочки матки. Чаще всего развивается как осложнение после всех видов абортов, диагностического выскабливания, введения внутриматочной спирали. Фоном для возникновения эндометрита часто служит существующий цервицит. Возникает после проникновения бактерий из канала шейки матки в эпителиальные клетки слизистой оболочки тела матки, иногда поражается мышечный слой - метроэндометрит. Эндометрит могут вызвать стафилококки, стрептококки, микоплазмы,  хламидии, гонококки.</p>


