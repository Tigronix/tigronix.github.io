<p> Острый  эндометрит проявляется на 3-4 день после занесения инфекции. Возможны тянущие или схваткообразные боли внизу живота, отмечается повышение температуры до 39С, нарушения менструального цикла, маточные кровотечения, обильные слизисто-гнойные выделения из канала шейки матки. Длительно текущий, не долеченный эндометрит переходит в хроническую стадию.</p>

<p>Для хронического эндометрита характерны  межменструальный маточные кровотечения, иногда «мажущего» характера, сгустки крови во время менструации, тянущие боли, гноевидные выделения из половых путей (цервицит, кольпит), периодическое повышение температуры до 37-38С. Это очень опасно и может привести к стойкому бесплодию</p>

<p><span class="bold">Только наши специалисты с успехом проводят диагностику и лечение данного заболевания</span></p>

<p>Диагностика эндометрита основана на жалобах пациентов, данных УЗИ, микроскопических и микробиологических</p>

