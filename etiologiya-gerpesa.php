<p>
Геномы ВПГ-1 и ВПГ-2 приблизительно на 50% гомологичны. Гомологичные участки распределены по всей карте гена. Большинство полипептидов, специфичных для одного типа вирусов, антигенно связаны с полипептидами другого типа. Вирусный геном упакован в капсид правильной икосаэдрической формы, состоящий из 162 капсомеров, С наружной поверхности вирус покрыт липид содержащей оболочкой, представляющей собой модифицированную клеточную мембрану, сформировавшуюся при проникновении ДНК-содержащего капсида сквозь внутреннюю ядерную мембрану клетки-хозяина. Между капсидом и двухслойной липидной оболочкой находится ещё одна оболочка, состоящая из ряда вирусных белков.<br />
<br />
Репликация вируса состоит из ядерной и цитоплазматической фаз. Начальные этапы репликации включают присоединение, слияние оболочки вируса с мембраной клетки с последующим высвобождением нуклеокапсида в цитоплазму клетки, распад нуклеокапсида и выход вирусной ДНК. После репликации вирусного генома и синтеза структурных белков в ядре клетки происходит сборка нуклеокапсидов.</p>

<p>По мере проникновения нуклеокапсидов из ядра через внутреннюю ядерную мембрану в перинуклеарное пространство они покрываются оболочкой. В некоторых клетках при репликации вируса в ядре формируются тельца-включения двух типов: базофильные тельца типа А, Фельген-положительные, содержат вирусную ДНК и эозинофшьное тельце-включение, свободное от вирусной нуклеиновой кислоты или белка, представляющее собой "шрам" вирусной инфекции. Затем вирионы транспортируются по эндоплазматической сети и пластинчатому комплексу к клеточной поверхности.</p>

	        	
		
