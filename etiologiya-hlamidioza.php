<p>Хламидии принадлежат к отдельной группе микроорганизмов эубактериальной природы. Они могут инфицировать широкий круг хозяев и вызывать самые различные заболевания. Микроорганизмы объединяются в единственный род Chlamydia, принадлежащий к единственному семейству Chlamydiaceae, входящему в порядок Chlamydiales. В настоящее время известно 3 вида хламидии - С. pneumoniae, С. psittaci, С. trachomatis.</p>

