<p>Хламидии образуют группу прокариотных микроорганизмов с уникальным циклом развития, который состоит из смены внутри- и внеклеточной фазы. Основными формами хламидии являются элементарные тельца (внеклеточные неподвижные сферические организмы) и ретикулярные (инициальные) тельца (внутриклеточные формы существования с типичной структурой грамотрицательных бактерий). Промежуточные формы, выявляемые в цикле развития, определяются как переходные или промежуточные.<br>
<br>
Все хламидии в процессе репродуцирования претерпевают ряд последовательных изменений. Зрелой формой, адаптированной к внеклеточному существованию, являются элементарные тельца. Элементарные тельца, являющиеся инфекционными формами микроорганизма, ответственны за процесс прикрепления к клетке-мишени и проникновения в нее. Маленькие (0,2-0,3 мкм) и оптически плотные элементарные тельца имеют ригидную малопроницаемую клеточную стенку, укрепленную прочными дисульфидными связями основного белка наружной мембраны (МОМР) и двумя богатыми цистеином белками: белком оболочки с молекулярной массой 60 кД и липопротеином наружной мембраны с молекулярной массой 12 кД.</p>

