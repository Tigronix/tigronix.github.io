<p><span class="bold">Папилломавирусы</span> чрезвычайно широко распространены и эпителиотропны. Это очень важно, так как известно, что большинство раковых опухолей человека представляют собой злокачественные образования, состоящие из эпителиальных клеток. В течение последних десяти лет установлено, что инфекция, вызванная ВПЧ, связана с развитием предраковых поражений половых органов - внутриэпителиальных неоплазий шейки матки, вульвы, влагалища, ануса, полового члена, а также плоскоклеточного рака.</p>

<p>Описаны различные пути, объясняющие генез карциномы из вирусных папиллом шейки матки. При некоторых исследованиях обнаруживали ДНК ВПЧ в нормальном неизменённом эпителии полового тракта у обоих полов, который выступает в качестве вирусного резервуара, скрывающего вирусную инфекцию. Папилломавирусная инфекция очень часто участвует в патологическом процессе при аногенитальных поражениях, хотя роль вируса в канцерогенезе остаётся до конца не выясненной. Вирусы папилломы человека считаются агентами, отвечающими за предраковые поражения гениталий и плоскоклеточный рак кожи у мужчин и женщин.</p>

<p>Существует множество других агентов вирусной и невирусной природы, которые действуют совместно или раздельно с ВПЧ в многоступенчатом процессе канцерогенеза.<br />
