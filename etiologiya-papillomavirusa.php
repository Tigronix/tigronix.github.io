<p><span class="bold">Сексуальное поведение:</span></p>

<ul class="list">
	<li>начало половой жизни в раннем возрасте;</li>
	<li>большое количество половых партнёров;</li>
	<li>частые половые контакты.</li>
</ul>

<p><span class="bold">Половые партнёры:</span></p>

<ul class="list">
	<li>партнёр, имевший половые связи с женщиной, которая умерла от рака шейки матки;  - партнёры с множеством половых связей;</li>
	<li>партнёры с генитальными поражениями, вызванными ВПЧ, или с карциномой полового члена;</li>
	<li>возможность химической канцерогенности спермы.</li>
</ul>

<p><span class="bold">Предвестники генитальных бородавок и ИППП:</span></p>

<ul class="list">
	<li>Chlamydia trachomatis - изменения, вызванные хламидиями, наблюдают во многих соскобах из шейки матки;</li>
	<li>Neisseria gonorrhoeae - наличие гонореи в анамнезе связано с неоплазиями вульвы;  - Trichomonas vaginalis - вызывают диспластические повреждения в слизистой при хронических вагинитах;</li>
	<li>Treponema pallidum, другие микробы: кандида, микоплазмы.</li>
</ul>

<p><span class="bold">Возраст:</span></p>

<ul class="list">
	<li>снижение частоты заболеваемости инфекции ВПЧ с увеличением возраста;</li>
	<li>наибольший процент инфицированных ВПЧ в группе 20-25 лет.</li>
</ul>

<p><span class="bold">Курение:</span></p>

<ul class="list">
	<li>метаболиты табака связаны с инфекциями шейки матки, вызванными ВПЧ.</li>
</ul>

<p><span class="bold">Гормональный статус:</span></p>

<ul class="list">
	<li>влияет на транскрипцию и/или трансляцию генома ВПЧ;</li>
	<li>влияние уровня эстрогенов и прогестерона на концентрацию IgA в цервикальном секрете (IgA обеспечивает местный иммунный ответ при развитии неоплазий).</li>
</ul>

<p><span class="bold">Беременность:</span></p>

<ul class="list">
	<li>увеличение частоты клинических проявлений.</li>
</ul>

<p><span class="bold">Использование оральных контрацептивов:</span></p>

<ul class="list">
	<li>увеличение частоты клинических проявлений инфекции ВПЧ при длительном использовании оральных контрацептивов.</li>
</ul>

<p><span class="bold">Введение диэтилстилбестрола в матку:</span></p>

<ul class="list">
	<li>стимуляция карциномы влагалища у молодых женщин, особенно при применении до 12 нед. беременности.</li>
</ul>

<p><span class="bold">Эндометриоз:</span></p>

<ul class="list">
	<li>наличие связи с первичным раком влагалища.</li>
</ul>

<p><span class="bold">Облучение шейки матки:</span></p>

<ul class="list">
	<li>возможность индукции рака влагалища;</li>
	<li>получение дозы, достаточной для мутагенного действия на влагалище;</li>
	<li>развитие карциномы in situ.</li>
</ul>

<p><span class="bold">Внутренние факторы:</span></p>

<ul class="list">
	<li>увеличение риска поражения ВПЧ при недостаточности витамина А, в-каротина, витамина С и фолиевой кислоты;</li>
	<li>защитная роль каротиноидов при канцерогенезе;</li>
	<li>низкое содержание фолатов в эритроцитах усиливает действие ВПЧ-16 при развитии неоплазий.</li>
</ul>

<p><span class="bold">Тип HLA:</span></p>

<ul class="list">
	<li>увеличение риска рака шейки матки у женщин с антигеном DQw3.</li>
</ul>

<p>В отличие от исследований, проведенных среди женщин, всего нескольким авторам удалось обнаружить различные кофакторы <span class="bold">инфекции у мужчин</span>, имеющих различные аногенитальные поражения, вызванные ВПЧ. Эпидемиология ВПЧ-инфекции у мужчин изучена в основном в популяции гомосексуалистов и у мужчин, серопозитивных на ВИЧ. Тем не менее, как и у женщин, доминирующим кофактором, способствующим ВПЧ-инфекции, является сексуальная активность.</p>

<p>На течение болезни влияет число и тип половых партнёров. Частые анальные сношения могут провоцировать ВПЧ-инфекцию в аноректальной переходной зоне, которая вызывает снижение местного иммунитета, нарушая функцию клеток Лангерганса. Доказано, что курение также увеличивает риск рака половых органов. Оба кофактора вызывают снижение местного иммунитета. Существует чёткая связь между обнаружением потенциально онкогенных типов ВПЧ и цитомегаловирусом человека.</p>

	        	
		
