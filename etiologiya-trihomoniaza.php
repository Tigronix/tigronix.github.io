<p><br />
Тело трихомонады состоит из оболочки, протоплазмы, блефаробласта, ядра, аксостиля, четырёх жгутиков, ундулирующей мембраны, фибрилл. В расширенной передней части тела трихомонады располагается удлиненное ядро с ядрышком. В ядре хорошо просматриваются глыбки хроматина. Обнаружение ядра существенно для идентификации трихомонад, так как фрагменты цитоплазмы, кусочки слизи и другие обрывки тканей могут иметь схожую с трихомонадами окраску. При электронно-микроскопическом исследовании установлено, что все тело трихомонады, включая жгутики, покрыто трёхслойной осмиофильной мембраной-перипластом; ядро состоит из мелкозернистой кариоплазмы; парабазальный аппарат - из системы нитей, расположенных в один или два ряда, а в цитоплазме находятся большие пищеварительные вакуоли.<br />
<br />
Размножается трихомонада путём продольного деления. Трихомонады не образуют цист или других форм устойчивости, обеспечивающих им сохранение вне человеческого организма.<br />
<br />
Влагалищная трихомонада представляет собой самостоятельный вид, в естественных условиях обитает только в мочеполовом аппарате человека и не поражает животных. У женщин местом обитания Trichomonas vaginalis является влагалище, у мужчин -предстательная железа и семенные пузырьки. Мочеиспускательный канал может поражаться у обоих полов.<br />
<br />
Трихомонады быстро теряют жизнеспособность вне человеческого организма. Во внешней среде при высушивании трихомонады погибают за несколько секунд, губительное действие на них оказывают температура выше 40°С, прямые солнечные лучи, медленное замораживание, различные антисептики. Трихомонады хорошо растут в анаэробных условиях, оптимум роста их при рН среды 5,5-7,5 и температуре 35-37°С. Трихомонады выделяют во внешнюю среду гиалуронидазу, питаются эндосмотически. Влагалищные трихомонады - аэротолерантные анаэробы со своеобразным энергетическим метаболизмом.</p>

	        	
		
