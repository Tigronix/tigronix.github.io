<p>Возбудителем мочеполового <a href="/lechenie-trihomoniaza/">трихомониаза</a> является <span class="bold">влагалищная трихомонада - Trichomonas vaginalis</span>.<br />
Она относится к простейшим - Protozoa, классу жгутиковых - Flagellate, роду трихомонад - Trichomonas.<br />
<br />
У человека паразитируют 3 вида трихомонад: Trichomonas tenax (в полости рта), Trichomonas hominis (в желудочно-кишечном тракте), Trichomonas vaginalis (в мочеполовом тракте). Урогенитальные трихомонады бывают трёх форм: грушевидной, амебовидной и почкующейся.<br />
