<span class="bold">Трихомонада представляет собой типичный одноклеточный паразит, признаками которого являются:</span>
<ul class="list">
	<li>способность повторять рельеф эпителиальной клетки (на которой они паразитируют), проникать между клетками в узкие щели и инвагинировать в клетку хозяина;</li>
	<li>способность урогенитальных трихомонад фиксировать на своей поверхности большие количества антитрипсина, что обеспечивает защиту простейших от разрушительного действия организма;</li>
	<li>вирулентность урогенитальных трихомонад, которая находится в прямой пропорциональной зависимости от их гемолитической активности;</li>
	<li>заразительность (развитие заболевания после полового контакта с больным трихомониазом);</li>
	<li>наличие на поверхности трихомонад протеолитических ферментов (гиалуронидаза, амилаза, катапаза);</li>
	<li>выраженный хемотаксис полиморфноядерных лейкоцитов (на поверхности трихомонад имеется фибропектин - гликопротеид, способствующий прикреплению паразита к лейкоцитам, эпителиальным клеткам, эритроцитам).</li>
</ul>
