<h2>Преимущества фармакологического борта:</h2>

<ul class="list">
	<li>после выполнения такой процедуры как фармаборт, можно без проблем забеременеть повторно</li>
	<li>при фармакологическом аборте процедура проводится, без каких либо хирургических вмешательств и не требует анестезии</li>
	<li>такой способ прерывания беременности, как фармаборт, не наносит такой большой стресс и психологическую травму, как при хирургическом вмешательстве</li>
	<li>осложнения при таком способе аборта бывают крайне редко, только в исключительных случаях</li>
</ul>

<p><img class="img_center" style="width: 500px; height: 375px; margin: 10px;" src="../img/303228d7b4d9.jpg" alt="фармакологический аборт (фармаборт)" /></p>

<div class="clr"></div>

<p>Подготовка к медикаментозному аборту является несложной, пациентке необходимо сдать анализ крови, мазок и ультразвуковую диагностику, дабы исключить внематочную беременность. После этого пациентка принимает специальный препарат и находится под наблюдением доктора несколько часов. В течении шести часов организм пациентки восстанавливается, что является самым главным плюсом этой процедуры. Так же пациентке доктор выписывает специальные поддерживающие препараты. После проведения медикаментозного аборта у женщины начинаются кровяные выделения, в первые несколько дней они обильные, и полностью проходят где-то на 15 день после процедуры. Дело в том, что этот препарат вызывает сокращение матки, что и приводит к выталкиванию плода, когда все выделения полностью прекращаются проводится повторное ультразвуковое обследование, для того что б исключить наличие остатков в матке. В целом послеаборный период проходит нормально, женщина может чувствовать легкое головокружение , слабость, усталость.</p>

<p>Осложнения при таком виде прерывания беременности, как фармаборт, крайне редки. Они могут возникнуть у женщин, которые прибегают к фармакологическому аборту уже не в первый раз, у женщин, у которых имеются гинекологические заболевания либо у тех, у кого были хирургические вмешательства.</p>

<h2>После медикаментозного аборта необходимо придерживаться некоторых правил:</h2>

<ul class="list">
	<li>сексуальный контакт возможен только после первой менструации, так как на данном этапе, влагалище женщины, как открытая рана, в которую легко можно занести инфекцию</li>
	<li>отказаться от каких-либо физических нагрузок</li>
	<li>не купаться в бассейнах и водоемах</li>
</ul>

<p>И самое главное предохраняйтесь, и тогда вам не нужно будет задумываться о проблеме аборта!</p>

	        	
		
