<p>
 Вагиноскопия при свежей <a href="/lecheniye-gonorei/">гонорее</a> позволяет констатировать отечность и гиперемию слизистых оболочек влагалища, особенно выраженные в области сводов влагалища и гиперемию шейки матки.<br />
<br />
 Одной из характерных черт гонореи у девочек является многоочаговость поражения, т.е. помимо половых органов, гонорейный процесс распространяется на уретру, прямую кишку, значительно реже на слизистые оболочки носа, глаз. У детей почти не бывает гонорейных артритов и миозитов.<br />
<br />
 Уретра вовлекается в процесс почти у всех девочек, страдающих острым гонорейным вульвовагинитом. Поражаются ее передняя и средняя части: наружное отверстие уретры расширено, губки гиперемированы и отечны. При надавливании на нижнюю стенку уретры выделяется гной. Дизурические явления резко выражены, вплоть до недержания мочи. Обильные гнойные бели, стекая из влагалища на промежность и соприкасающиеся поверхности бедер, раздражают и мацерируют кожу, способствуют инфицированию прямой кишки.<br />
<br />
 Гонорейный проктит распространяется на небольшой (3-4 см проксимальнее сфинктера) отрезок прямой кишки. При осмотре устанавливается также отечность и гиперемия складок вокруг заднего прохода, нередко трещины и гнойное отделяемое. При вовлечении в процесс цервикального канала помимо отечности и гиперемии влагалища и шейки матки из зева выделяется обильный гной. Частота эндоцервицитов колеблется в зависимости от возраста и общего состояния ребенка. Наиболее часто их обнаруживают у девочек от 3 до 7 лет, а также при наличии туберкулезной интоксикации или очагов хронической инфекции. В результате лечения наступает полная инволюция процесса.  Иногда с самого начала гонорейный процесс принимает торпидное течение.<br />
<br />
 Хроническая гонорея чаще, чем свежая осложняется бартолинитами, скеннитами, эндоцервицитами, особенно у девочек старшего возраста. При хроническом течении большие и малые половые губы могут оставаться неизмененными, но иногда они пастозны, с очаговой гиперемией на внутренней поверхности малых половых губ.</p>

<p>Симптоматика поражения бартолиниевых и парауретральных желез, скенновых ходов незначительна. Уретра вовлекается почти всегда, течение стертое, дизурические явления выражены слабо или отсутствуют. Отмечается гиперемия и отечность слизистой оболочки задней спайки и складок гимена, где задерживается небольшое количество гнойного отделяемого.</p>

<p>В значительном количестве гной находят при вагиноскопии в сводах влагалища. На слизистой влагалища вместо диффузной гиперемии, которая характерна для свежей, острой гонореи, имеются отдельные очажки застойной гиперемии и пастозности. Во влагалище может наблюдаться пролиферация сосочков в виде зернистых точек (гранулезный вагинит), слизистая напоминает "малину".  Влагалищная часть шейки матки бледная, синюшная, иногда по нижнему краю зева видна эрозия.<br />
<br />
 Хронический гонорейный эндоцервицит у ослабленных девочек может распространяться на эндометрий, чему способствует отсутствие внутреннего зева, и продолжение складок цервикального канала в полость матки. Однако вовлечение в процесс вышележащих отделов половых органов происходит редко.<br />
<br />
 У менструирующих девочек может развиться восходящая гонорея с поражением придатков и тазовой брюшины. Иногда восходящая гонорея сопровождается нарушением общего состояния, повышением температуры тела, резкими болями внизу живота, перитонеальными явлениями.</p>

	        	
		
