<p>При свежей острой гонорее в процесс вовлекаются кожа малых половых губ, слизистая оболочка входа во влагалище, само влагалище, уретра и нижний отдел прямой кишки. Развивается яркая диффузная гиперемия и отечность клитора, больших половых губ и промежности. Из влагалища на вульву стекает обильное гнойное отделяемое, засыхающее в корки, располагающиеся на гребне больших и малых половых губ. Гименальное кольцо валикообразно утолщено, гиперемировано. Местами на слизистой оболочке вульвы видны небольшие эрозии. Паховые лимфоузлы увеличены и болезненны.</p>

