 <p>Наличие в семье лиц старшего поколения, инфицированных хламидиями (блефароконъюнктивиты, урогенитальная патология) представляет угрозу инфицирования остальных членов семьи. Предложен термин "семейный хламидиоз", так как обследование семьи позволяет выделить хламидийные поражения в двух-трех поколениях (дедушки, бабушки, дети и внуки). Примерно 30-35% детей в семьях, где родители больны урогенитальным хламидиозом, также поражены этим заболеванием, причём нередко (до 7 %) - это экстрагенитальные формы. Чаще при этом наблюдаются вялотекущие конъюнктивит или блефаро-конъюнктивит.<br />
<br />
Симптоматика хламидийных заболеваний у детей более выражена, чем у взрослых, и в зависимости от давности воспалительного процесса проявляется в виде вульвовагинита, уретрита, цервицита. Болеют чаще девочки, особенно в старшем возрасте, когда заражение происходит половым путём. Наряду с диффузной гиперемией вульвы и прилегающих участков кожного покрова, отмечается отёк наружных половых органов, мацерация их в результате обильных, жидких, серо-зелёного цвета цвета выделений. При вагиноскопии с помощью женского уретроскопа можно наблюдать отёчность, гиперемию слизистых оболочек влагалища и участков, прилегающих к шейке матки; шейка рыхлая, девственная плева гиперемирована и пастозна. При поражении мочеиспускательного канала губки гиперемированы, отёчны, покрыты отделяемым серого цвета, возможно учащённое и болезненное мочеиспускание.</p>

	        	
		
