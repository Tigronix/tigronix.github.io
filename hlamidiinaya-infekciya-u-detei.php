<p>Заражение детей происходит в основном при прохождении через родовые пути инфицированной матери, либо через предметы личной гигиены. Пренатальный скрининг беременных женщин может предупредить развитие хламидийной инфекции у детей. Особенно рекомендуется проведение скрининга у беременных женщин моложе 25 лет, имеющих нового или многочисленных партнеров. Инфекция, вызванная С. trachomatis, у новорожденных является следствием перинатального инфицирования из шейки матки матери.</p>
