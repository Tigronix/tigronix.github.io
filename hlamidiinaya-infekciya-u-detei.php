<p>Первоначально <a href="/lecheniye-khlamidioza-v-moskve/">хламидиоз</a> поражает слизистые оболочки глаз, ротоглотки, урогенитального тракта и прямой кишки. Инфекция у новорожденных часто распознается на основании симптомов конъюнктивита, развивающегося через 5-12 дней после рождения, Хламидии чаще всего являются причиной офтальмии новорожденных. Рекомендуется всех младенцев с конъюнктивитом в возрасте до 30 дней обследовать на хламидии. С. trachomatis также является наиболее частой причиной подострых, не сопровождающихся подъёмом температуры пневмоний, развивающихся на  1-3 месяце жизни ребенка. У ребенка также могут наблюдаться бессимптомные инфекции ротоглотки, генитального тракта и прямой кишки.</p>
