 <p>При поражении хламидиями труб и эндометрия нарушается процесс плацентации и развития последа, что способствует развитию вторичной плацентарной недостаточности. Хламидии могут поражать плаценту и оболочки, вызывать образование патогенных иммунных комплексов, что существенно нарушает функцию последа как органа, обеспечивающего плоду адекватное питание и дыхание. В подобных случаях в последе обнаруживаются признаки истощения компенсаторно-приспособительных реакций, инволютивно-дистрофические процессы, диссоциация развития элементов стромы и ворсин, избыточное отложение фибриноида и лимфоцитарная инфильтрация. Морфофункциональные изменения последа ведут к плацентарной недостаточности, что в свою очередь приводит к задержке развития и гипотрофии плода. Наиболее высок риск поражения последа при инфицировании женщины во время беременности. В тех случаях, когда беременность наступает на фоне хронической хламидийной инфекции, наличие у женщины антихламидийных антител препятствует распространению инфекционного процесса.<br />
<br />
 Наличие инфекции у беременной может являться причиной осложненного течения послеродового периода. У инфицированных хламидиями женщин эндометриты наблюдаются в 4 раза чаще и, как правило, на 2-б неделе после родов. Они характеризуются отсутствием явных признаков острого инфекционного процесса. Нередко единственным признаком заболевания является наличие ранней и длительной субфебрильной температуры, сочетающейся с замедленной инволюцией матки. При наличии у родильницы отягощенного соматического и акушерско-гине-кологического анамнеза и нарушений ранней постнатальной адаптации у ребенка, необходимо и мать и ребенка обследовать на хламидиоз.</p>

	        	
		
