<h2><a href="/konsultacia_urologa/prostatit/">Простатит</a></h2>

<p><img style="width: 240px; height: 180px; margin: 10px; float: left;" src="../img/princip-prostatita3.jpg" alt="простатит - уролог в Москве" />Воспалительное поражение предстательной железы, возникающее по разным причинам – вследствие переохлаждения, гормональных нарушений, снижения иммунитета, травмирования органов, воздействия инфекций, передающихся половым путем и др. Простатит характеризуется ощущением озноба и повышением температуры, болями в промежности и паховой области, частыми и болезненными мочеиспусканиями либо острой задержкой мочи. В некоторых случаях пациенты отмечают наличие гнойных выделений. Запущенный воспалительный процесс может привести к заражению крови.</p>

