<h2><a href="/konsultacia_urologa/mochekamennaya-bolezn/">Мочекаменная болезнь</a></h2>

<p><img style="width: 250px; height: 246px; margin: 10px; float: left;" src="../img/mochekamennaya-bolezn-kamni-big.jpg" alt="мочекаменная болезнь" /></p>

<p>Заболевание, характеризующееся образованием камней в органах мочевыделительной системы. Среди основных причин называют инфекции мочевыводящих путей и нарушения обменных процессов в организме. Мочекаменная болезнь приводит к закупорке мочеточника, вызывающей острые поясничные боли, появление крови в моче, тошноту и рвоту. Несвоевременное обращение на прием к врачу может привести к развитию воспаления почек и необходимости госпитализации.</p>

