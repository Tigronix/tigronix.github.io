<h2><a href="/konsultacia_urologa/uretrit/">Уретрит</a></h2>

<p><img style="width: 300px; height: 163px; margin: 10px; float: left;" src="../img/uretrit.jpg" alt="консультация уролога - уретрит" />Воспаление уретры, т.е. мочеиспускательного канала, которое вызывают бактерии и вирусы. К основным симптомам относят болезненное мочеиспускание, гнойные выделения, покраснение краев отверстия канала. При отсутствии лечения уретрит может привести к поражению других отделов мочеполовой системы, сужению уретры, простатиту и даже опухоли яичек.</p>

