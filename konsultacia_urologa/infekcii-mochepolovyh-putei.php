<p>Количество бактерий, попадая в органы мочевой системы, значительно увеличивается. Инфекция может попадать через уретру,  через гематогенный и лимфогенный пути заражения.</p>

<p>Очень распространенно заражение кишечной палочкой. Это заболевание возникает из-за снижения иммунитета и тогда кишечная палочка попадает в мочевые пути, тем самым вызывая воспаление. Этот процесс немного похож на состояние при попадании половых инфекций. Кроме мочевых путей они могут распространяться на репродуктивные органы. Более часто это происходит при заболевании хламидиозом и микоплазмосом.</p>

<p>Кишечная палочка обитает в кишечнике и области ануса. Если не соблюдать нормы гигиены инфекция попадает в мочеполовую систему, вызывая воспаление. Зачастую при попадании инфекции она удаляется из организма вместе с током мочи. Однако при слабом иммунитете и несоблюдении личной гигиены инфекция может задержаться в мочевой системе. Оставаясь там, инфекция может двигаться дальше. Таким образом, спровоцировав заражение мочеточника и почек.  </p>

<p>В зоне риска находятся люди, страдающие камнями в почках, хроническими заболеваниями которые провоцируют не полное опорожнение мочевого пузыря, имеющие подавленный иммунитет. Также пациенты, которые принимают иммунодепрессанты во время химиотерапии, женщины с беспорядочными половыми связями. Для женщин в качестве профилактики заболевания рекомендуется сразу после полового акта опорожнять мочевой пузырь, таким образом выводить возможные бактерии из мочевых путей. Также инфекции могут возникнуть у женщин пользующимся таким способом контрацепции как колпачок. </p>

<p>Рискуют заболеть мужчины с увеличенной предстательной железой, аденомой простаты. Теоретически мужчины менее подвластны заболеваниям мочеполовой системы, так как у них протяженность уретры намного больше, чем у женщин.</p>

<p>Также ученые утверждают, что инфекционные заболевания  часто развиваются у офисных работников, водителей, людей работающих в холодном помещении. Кроме того, даже дети любых возрастов могут заразиться инфекциями мочеполовой системы.</p>

<p>Дети грудного возраста могут получить инфекцию через кровь, маленькие дети могут не соблюдать правила личной гигиены, могут возникать врожденные аномалии.</p>

<p>При подозрении на инфекционные заболевания мочеполовой системы необходимо сразу же обратиться к врачу!</p>

	        	
		
