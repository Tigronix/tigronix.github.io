		<li>диагностику гиперактивности мочевого пузыря (ГАМП).</li>
	</ul>

	<p>В зависимости от причин и тяжести заболевания, а также на основании клинических данных и индивидуальных особенностей пациента, назначается медикаментозное лечение энуреза. Совместно пациенту может быть назначен курс лечения у невропатолога, гомеопатия, точечный массаж, лазерное излучение и др. Важно то, что в лечении энуреза у взрослых применяется сочетание приема лекарственных препаратов с коррекцией поведения. В том случае, если использованные методы не дают результата, назначается операция по лечению энуреза.</p>
	
