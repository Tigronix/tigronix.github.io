	<h2>Виды гидронефроза</h2>
	
	<p>Различают острый и хронический гидронефроз. Признаки острого гидронефроза проявляются рвотой, тошнотой, повышением температуры тела. Также пациенты, страдающие этим заболеванием, жалуются на боли в животе, в области поясницы. Возникают болевые ощущения и после употребления жидкости. Хронический гидронефроз протекает практически бессимптомно, либо периодически проявляются такие же симптомы, как при остром гидронефрозе. </p>
	
	<p>Лечение гидронефроза может быть медикаментозным, но в таком случае оно направляется на уменьшение болевого синдрома. В большинстве случаев лечение этого заболевания требует хирургического вмешательства. Целью операции по лечению гидронефроза является восстановление нормального оттока мочи. Для этого применяются следующие виды оперативных вмешательств: лапароскопическая пластика пиелоуретрального сегмента, открытые пластические вмешательства на лоханочно-мочеточниковом сегменте и эндоскопические урологические вмешательства (баллонная дилятация, бужирование, стентирование). </p>
	
