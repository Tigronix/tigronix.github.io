	<h2>Новообразования мочеполовой сферы: диагностика, лечение</h2>
	
	<p>Для выявления опухолевидных новообразований используется ультразвуковое обследование, цитологическое, лапароскопия, биопсия. Также выполняются компьютерная томография, магнитно-резонансная томография, анализы на онкомаркеры.
Лечение новообразований мочеполовой сферы осуществляется с учетом особенностей клинической картины пациента на основе выполненного диагностического обследования.
Для подавления развития опухолей возможны малотравматичные оперативные вмешательства, частичное или полное удаление пораженного органа. Используется также лучевая терапия и химиотерапия. Главным залогом успешного лечения новообразований мочеполовой сферы является ранняя диагностика заболевания, поэтому, чем раньше пациент обратится к врачу, тем больше шансов на выздоровление.</p>

