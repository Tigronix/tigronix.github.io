<p>Чаще всего камни ущемляются внизу мочеиспускного канала, так как там у мочевого пузыря наиболее тонкий просвет.</p>

<p>Однако, причиной возникновения почечной колики становятся не только каменные образования. Есть ряд других причин, провоцирующих эту резкую боль. Почечная колика возникает при закупорке мочеточника сгустками крови, отслоившимся от опухоли. Также возникает при сдавливании мочеточника новообразованиями. Если почка «опускается» и перегибает мочеточник ,также возникает резкая почечная колика.</p>

<p>Симптомы почечной колики можно спутать с острым животом. Однако, отличие в том, что боль возникает, если положение больного вертикально. А если человек приляжет, то боль при почечной колике утихнет. Такое ощущение может возникнуть вследствие физического труда, тяжелых нагрузок.</p>

<p>Симптомы почечной колики.</p>

<p>Изначально человек чувствует острую сильную боль, которая как бы распирает изнутри. В основном, боль образуется со стороны, где происходит сдавливание мочеточника или почки. Бывает, что боль охватывает область живота вместе с поясницей. Если, у Вас камни застряли внизу мочетока, боль отдает в область паха, половых органов, в бедра. Если же камни остановились выше, то боль может образоваться в подложечной области или в животе.</p>

<p>Во время почечной колики возникают спазмы мочеточника и почек. Это происходит, потому что организм пробует избавиться от препятствия. Больные во время приступа могут испытывать страх и беспокойство, пытаются унять боль переменой положения тела. Колика вызывает частые ложные позывы в туалет, а само мочеиспускание может быть очень болезненным.</p>

<p>Когда приступ проходит, происходит разрыв вены камнем. В моче появляется кровь, однако человеческому глазу она ее не видно. Определить это можно лишь в лабораторных условиях.</p>

<p>Если моча выходит с кровью в самом начале приступа, это может быть симптомом онкологического заболевания.</p>

<p>Помимо болевых ощущений, возникает рвота, вздутие живота, задержка газов, приостановка перистальтики. Вместе с коликой человек ощущает сильную резкую боль в брюшной полости.</p>

<p>Почечная колика начинается и прекращается внезапно. Часто вместе с мочой выходят мельчайшие частички камней, которые спровоцировали приступ.</p>

<p>Почечная колика приносит сильную боль и муку, поэтому во время приступа человеку необходима врачебная помощь. Основное лечение состоит в ведении анальгетиков и спазмолитиков. Вместе с этим рекомендуют принять теплую ванну или приложить грелку. Однако, это можно делать если заболевание брюшной полости абсолютно исключено, и четко поставлен диагноз о почечной колике. Именно поэтому не стоит лечиться самостоятельно, а необходимо сразу же обратится к врачу. Если почечная колика учащается, то больного необходимо госпитализировать. В стационаре врачи окажут должную медицинскую помощь. Камни могут быть удалены эндовезикальным путем или хирургическим.</p>

	        	
		
