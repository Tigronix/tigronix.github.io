<p>В каких случаях требуется консультация андролога?</p>

<p>Консультация андролога нужна если Вы чувствуете<span class="bold"> боли в области пениса и мошонки</span>. В этих случаях, Вам необходимо незамедлительно обратиться к врачу. Болезненные ощущения могут быть симптомами простатита, паховой грыжи, воспалительных и инфекционных заболеваний.</p>

<p><span class="bold">Нарушения эрекции</span>. Если Вы страдаете эректильной дисфункцией, запишитесь на прием андролога. Причинами снижения потенции могут быть воспалительные заболевания половых органов, неврологические расстройства, падение уровня тестостерона в крови. Андролог проведет необходимую диагностику и назначит курс лечения после приема.</p>

<p><img class="img_center" style="width: 480px; height: 360px; margin: 10px;" src="../img/erection.png" alt="прием и консультация андролога" /></p>

<div class="clr"></div>

<p><span class="bold">Нарушения эякуляции</span>. Если Вы страдаете преждевременным/пролонгированным семяизвержением или полным отсутствием эякуляции, Вам необходима срочная помощь. Причинами нарушений может быть травма спинного мозга, повышение чувствительности головки пениса и эндокринные заболевания.</p>

<p><span class="bold">Подозрение на венерическую инфекцию</span>. Если у Вас был незащищенный половой контакт, после которого Вы заметили сыпь в паховой области или выделения из мочеиспускательного канала, срочно обратитесь за помощью к андрологу. Перечисленные симптомы могут свидетельствовать о заражении хламидиозом, сифилисом, трихомониазом и другими инфекциями.</p>

<p><span class="bold">Невозможность зачатия</span>. Если Ваша полностью здоровая партнерша не может забеременеть в течение 6-12 месяцев регулярной половой жизни, и при этом Вы не пользовались контрацептивами, запишитесь на прием андролога. Причинами бесплодия могут быть воспалительные и инфекционные заболевания половых органов, иммунная несовместимость партнеров и другие проблемы репродуктивной сферы, выявить которые поможет врач.</p>

<p><span class="bold">Гинекологические и андрологические заболевания – главная причина бесплодия!</span></p>

<p>Если Вы хотите сохранить свое репродуктивное здоровье, регулярно посещайте врачей. Помните, что даже при отсутствии симптомов заболеваний половой сферы профилактический осмотр у данных специалистов нужно проходить не реже 1 раза в год. Чтобы записаться на прием к специалистам нашего медицинского центра, звоните по телефону <span style="font-size:18px;"><span class="bold">+7 (495) 256-38-00</span></span>.</p>

		
