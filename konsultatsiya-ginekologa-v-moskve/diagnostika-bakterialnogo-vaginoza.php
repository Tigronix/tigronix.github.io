<p align="center" style="text-align: justify;"><span class="bold">Специалисты нашего центра проведут профессиональную диагностику вагиноза и назначат эффективное лечение</span></p>

<p><span class="bold">Бактериальный вагиноз</span> – это инфекционное гинекологическое заболевание, связанное с резким снижением лактофлоры влагалища и ее заменой на гарднереллы и анаэробные бактерии. При вагинозе концентрация полимикробных ассоциаций во влагалищных выделениях составляет 10<sup>9</sup>—10<sup>11</sup> КОЕ/мл.</p>

<h2>Симптомы вагиноза</h2>

<p><span class="bold">Выделения из влагалища</span>. Самый распространенный симптом бактериального вагиноза – обильные влагалищные выделения, которые могут иметь неприятный несвежий запах. После полового акта и перед менструальными кровотечениями выделений становится больше. В зависимости от стадии заболевания бели могут быть различного цвета. На первой этапе развития болезни характерны белые или серые выделения, а при прогрессировании патогенного процесса они становятся желтыми или зелеными. По консистенции бели бывают тягучими, густыми, липкими и иногда пенящимися.</p>

<p><span class="bold">Жжение и зуд во влагалище</span>. Одним из распространенных симптомов вагиноза является дискомфорт во влагалище и зоне промежности. По данным исследований, 26% страдающих этим заболеванием женщин жалуются на постоянный зуд во влагалище, а 28% больных испытывают жжение в области промежности и наружных половых органов. Дискомфорт может усиливаться во время мочеиспускания или полового акта. Для многих женщин зуд и жжение становятся причинами неврозов.</p>

<p><span class="bold">Нарушения мочеиспускания</span>. На дизурические расстройства жалуются около 15% больных, страдающих бактериальным вагинозом. Во время опорожнения мочевого пузыря женщины могут испытывать боль, резь и жжение в мочеиспускательном канале. Симптомами бактериального вагиноза также являются слишком частые или, наоборот, редкие мочеиспускания, а также энурез.</p>

<p><span class="bold">Боль в процессе полового акта</span>. Достаточно распространенным симптомом бактериального вагинита является диспареуния, на которую жалуются около 21% больных. Во время полового акта женщины ощущают боли во влагалище, промежности, нижней части живота. Неприятные ощущения могут стать причиной аноргазмии, снижения полового влечения, психологических и неврологических расстройств.</p>

<h2>Методы диагностики</h2>

<p><span class="bold">Сбор анамнеза</span>. Первым этапом диагностики бактериального вагиноза является беседа с пациенткой. Для постановки верного диагноза требуется подробная информация о времени появления первых симптомов заболевания, характере жалоб, наличия в анамнезе воспалительных или инфекционных заболеваний половых органах и эффективности их лечения. Чаще всего женщины, страдающие вагинозом, неоднократно ранее обращались к таким специалистам, как гинеколог, миколог, уролог, невропатолог и эндокринолог с жалобами на влагалищные выделения, расстройства мочеиспускания, жжение и зуд в промежности и на другие характерные симптомы. При этом заболевание могло быть диагностировано, но лечение не имело положительного эффекта. Согласно данным статистики, 75% женщин, повторно обращающихся за медицинской помощью, ранее принимали антибактериальные препараты.</p>

<p><span class="bold">Физикальное обследование</span>. В процессе объективной диагностики обследуется состояние слизистой влагалища, наружных половых органов и уретры, шейки матки. На этом этапе определяется характер влагалищных выделений. При бактериальном вагинозе последние имеют характерный резкий «рыбный» запах. По состоянию белей определяется стадия заболевания. Выделения распределяются по стенкам влагалища равномерно и легко удаляются при помощи ватного тампона. Количество выделений превышает норму в 5-10 раз (примерно 20 мл в сутки). При бактериальном вагинозе во время осмотра не выявляются симптомы отека, гиперемии или воспаления влагалищных стенок, отсутствуют точечные кровоизлияния. Для слизистой оболочки характер розовый цвет. В 39% случаях при осмотре больных, страдающих вагинозом, выявляются патологии шейки матки (рубцы, эрозия, цервицит).</p>

<p><span class="bold">Аминовый тест и измерение pH</span>. Для точной диагностики бактериального вагиноза проводится аминовый тест. Врач производит забор влагалищной жидкости (мазок) и добавляет к ней 10% раствор КОН. Положительным результатом теста считается появления запаха гнилой рыбы, который связан с присутствием в мазке летучих аминов. При бактериальном вагинозе в выделениях могут присутствовать следующие продукты декарбоксилирования аминокислот: триметиламин, кадаверин, диметиламин, путресцин. Еще один тест, используемый для диагностирования заболевания, – измерение pH влагалищной среды. При этом методе исследования во время осмотра используются индикаторные полоски с величиной делений не более 0,2. При бактериальном вагините выявляется сдвиг в сторону щелочной среды (около 6,0).</p>

<p><span class="bold">Лабораторное исследование</span>. Основным лабораторным диагностическим методом, применяемым при подозрении на вагиноз, является микроскопия влагалищных выделений, взятых из области заднего свода, и окраска по Граму. В процессе исследования проверяется наличие/отсутствие микроорганизмов Mobiluncus spp, оцениваются морфотипы бактерий, измеряется количество лейкоцитов. При бактериальном вагините при окрашивании мазков по Граму обнаруживается присутствие в выделениях «ключевых клеток», которые представлены клетками эпителиальной ткани влагалища с адгезированными кокками и грамвариабельными палочками.</p>

<h2>Последствия бактериального вагиноза</h2>

<p>При отсутствии надлежащего лечения вагиноз может вызывать следующие осложнения: воспалительные заболевания органов малого таза, цервицит (воспаление матки), цервикальную дисплазию шейки матки, эндометрит, нарушение половой функции (снижение либидо, аноргазмия), неблагоприятный исход беременности (самопроизвольный аборт, преждевременные роды) и акушерские осложнения. Заболевание также может увеличить восприимчивость организма женщины к ВИЧ-инфекции.</p>

