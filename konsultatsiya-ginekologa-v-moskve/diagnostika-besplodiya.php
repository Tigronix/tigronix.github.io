<h2 style="text-align: center;"><span class="bold">Диагностика мужского и женского бесплодия </span></h2>

<p><span class="bold">Методы диагностирования мужского бесплодия:</span></p>

<ul class="list">
	<li>Диагностика бесплодия начинается со<span class="bold"> сперматограммы</span>. Это основной метод выявления мужского бесплодия. С помощью лабораторного исследования эякулята проводится диагностика бесплодия и определяется количество, морфологические характеристики и подвижность сперматозоидов, количество незрелых клеток сперматогенеза, типы и количество лейкоцитов. В результатах сперматограммы указывают объем, цвет, вязкость, pH и время разжижения эякулята. Данные, полученные с помощью этой лабораторной методики, позволяют установить фертильность мужчины.</li>
</ul>

<p><img style="width: 350px; height: 403px; margin: 10px; float: left;" src="../../img/diagnostika-besplodiya1.jpg" alt="диагностика бесплодия у мужчин" /></p>

<ul class="list">
	<li><span class="bold">Биопсия яичек</span>. Диагностика используется, чтобы выявить мужское бесплодие, если сперматозоиды не могут естественным путем покинуть яички, например, в результате нарушения проходимости семявыносящих протоков. Биопсия представляет собой взятие тканей яичек для последующего лабораторного исследования. Данная диагностика бесплодия позволяет определить степень и характер нарушения сперматогенеза, вызвавшего бесплодие, а также возможность использования мужских половых клеток для вспомогательной репродуктивной терапии. По итогам процедуры лечащий врач может подтвердить или опровергнуть диагноз бесплодие, поставленный на основе анализа сперматограммы.</li>
	<li><span class="bold">Термографический анализ мошонки</span>. Методика используется для выявления заболеваний органов мошонки, которые могут вызвать бесплодие. Анализ состояния мошонки проводится с помощью дистанционного тепловизора или термографической пластины. Этот метод исследования на бесплодие позволяет определить характер паховой грыжи, стадию варикоцеле или гидроцеле. Термографический анализ назначается при выявленных патологиях спермы и подозрением на бесплодие.</li>
</ul>

<div class="clr"></div>

<p><span class="bold">Методы диагностирования женского бесплодия:</span></p>

<ul class="list">
	<li><span class="bold">УЗИ малого таза</span>. С помощью ультразвукового исследования выявляются врожденные и приобретенные патологии строения матки и яичников, эндометриоз, миома и другие заболевания, вызывающие женское бесплодие. Этот метод позволяет определить наличие/отсутствие беременности, в том числе внематочной. УЗИ используется не только для того, чтобы выявить бесплодие, но и выбора оптимального времени оплодотворения, так как метод позволяет оценить состояние матки, размеры фолликула и яйцеклетки.</li>
</ul>

<p><img style="width: 350px; height: 300px; margin: 10px; float: left;" src="../../img/diagnostika-besplodiya.jpg" alt="узи при диагностике бесплодия" /> <img style="width: 350px; height: 300px; margin: 10px; float: left;" src="../../img/zhenskoe-besplodie-trubnoe-besplodie-9.jpg" alt="диагностика бесплодия -лапароскопия" />
<div class="clr"></div>

Узи малого таза                                                                        Лапароскопия и гистероскопия</p>

<ul class="list">
	<li><span class="bold">Лапароскопия</span>. С помощью этой методики исследования органов малого таза диагностируется эндометриоз, миома, спайки маточных труб, а также ряд воспалительных заболеваний, которые могут вызвать бесплодие. В процессе лапароскопии, которая представляет собой микрохирургическое вмешательство, лапароскоп (оптическая система с возможностью увеличения изображения до 40 раз) вводится через небольшой надрез в передней стенке брюшной полости пациентки. Восстановление после процедуры занимает в среднем 6 суток.</li>
	<li><span class="bold">Гистероскопия</span>. Эта процедура используется не только для выявления дисфункций, вызывающих бесплодие, но и непосредственного лечения органов женской репродуктивной системы. Во время гистероскопии в полость матки через влагалище вводится оптическая система, которая позволяет детально обследовать состояние стенок матки. В процессе гистероскопии врач может сделать биопсию измененного участка тканей, произвести рассечение внутриматочных перегородок, синехий и спаек, удалить инородные тела и остатки плодного яйца, а также прижечь миоматозные образования, вызвавшие бесплодие.</li>
	<li><span class="bold">Гистеросальпингография</span>. Этот метод позволяет диагностировать нарушение строения репродуктивных органов и заболевания, которые могут вызвать бесплодие. Во время процедуры с помощью введенного через влагалище катетера полость матки и маточных труб заполняется контрастным веществом. Затем делается рентгеновский снимок малого таза, на котором контраст отображается черным цветом. По результатам исследования можно судить о проходимости маточных труб, наличии/отсутствии внутриматочной перегородки, спаек, миоматозных узлов, эндометриоза.</li>
</ul>

<p><img class="img_center" style="width: 500px; height: 405px;" src="../../img/diagnostika-besplodiya2.jpg" alt="диагностика бесплодия - гистеросальпингография" /></p>

