<p>Гистеросальпингография</p>

<p>Во время обследования на бесплодие проводятся проверки у обоих партнеров:</p>

<ul class="list">
	<li><span class="bold">Проба Шуварского</span>. Тест позволяет выявить иммунологическую несовместимость партнеров, которая является одной из причин проблем с зачатием. Для исследования берутся пробы спермы после 5-6 суток воздержания и цервикальная слизь. При смешении этих жидкостей наблюдают состояние сперматозоидов. Если в течение 2 часов с начала теста сперматозоиды гибнут или теряют подвижность, то это подтверждает иммунологическую несовместимость партнеров. Если в слизь маточного канала проникло более половины всех сперматозоидов, то результат теста на бесплодие считается отрицательным.</li>
	<li><striong>Определение уровня гормонов. Исследование гормонального фона позволяет выявить эндокринные нарушения. Для определения уровня гормонов щитовидной железы, пролактина, тестостерона и других женских и мужских половых гормонов берется кровь из вены.</striong></li>
</ul>

<p>Неблагоприятные факторы:</p>

<ul class="list">
	<li>Возраст старше 30 лет</li>
	<li>Наличие венерических заболеваний</li>
	<li>Употребление алкоголя</li>
	<li>Курение</li>
	<li>Хронические стрессы</li>
	<li>Гиподинамия</li>
	<li>Ношение тесного белья также может вызвать бесплодие</li>
</ul>

