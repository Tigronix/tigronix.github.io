<p>Исследования показали, что вероятность заражения половыми инфекциями выше у женщин которые употребляют гормональные контрацептивы, так как гормоны снижают защиту организма от таких инфекций.</p>

<p>Сейчас существует много различных половых инфекций: трихомониаз, герпес, уреаплазмос, хламидиоз, гарднерелез, микоплазмос и другие, последствия этих заболеваний не так страшны, как, к примеру, ВИЧ или сифилис, но они расшатывают иммунную систему человека, что приводит к другим заболеваниям.</p>

<p>Симптомы, связывающие все заболевания это: зуд, жжение, выделения с неприятным запахом. Если такие заболевания не лечить, то возможно даже бесплодие. Поэтому при проявлении вышесказанных симптомов женщине необходимо обратится наш медицинский центр на Авиамоторной в Москве для дальнейшего обследования и лечения, ведь многие женщины даже не знают об присутствии заболеваний, поэтому каждые пол года необходимо посещать гинеколога и проходить осмотр, ведь никто от этого не застрахован. Так же на появление половых инфекций влияет образ жизни женщины, ведь курение, алкоголь и наркотики очень сильно снижают иммунитет, и вероятность  появления такой инфекции во много раз превышает, нежели заболевание женщины, которая ведет здоровый образ жизни.</p>

	        	
		
