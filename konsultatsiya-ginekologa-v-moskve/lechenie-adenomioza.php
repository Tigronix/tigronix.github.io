<h2>Клинические признаки аденомиоза матки
		</h2>
<p>Заболевание такого рода характеризуется появлением следующих симптомов:
	</p><ul>
	<li>наличие светло-коричневых выделений из влагалища до и после менструации;</li>
	<li>нарушения менструального цикла;</li>
	<li>обильные выделения во время менструального цикла;</li>
	<li>болезненные ощущения во время полового акта;</li>
	<li>болевые ощущения внизу живота и в области малого таза;</li>
	<li>предменструальный синдром.</li>
</ul>
<h2>Диагностика и лечение аденомиоза матки в Москве: доступные цены, эффективная медицинская помощь 
	</h2>
<p>Эффективность лечения аденомиоза матки зависит от своевременности постановки диагноза, определения типа и степени заболевания. В связи с этим диагностика включает: гинекологический осмотр, лабораторные анализы, кольпоскопию и гистероскопию, УЗИ органов малого таза, МРТ. После установки диагноза, врач подбирает оптимальный способ лечения аденомиоза матки. В частности, для устранения недуга используются два основных метода:
	</p><ul>
	<li>медикаментозная терапия. Эффективна при небольших размерах очага и при начальной стадии заболевания.</li>
	<li>хирургическое вмешательство. Рекомендуется при тяжелых формах аденомиоза матки.</li>
</ul>
