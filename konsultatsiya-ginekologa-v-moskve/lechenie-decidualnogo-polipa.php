<h2>Симптомы децидуального полипа
		</h2>
<p>Начинать лечение децидуального полипа следует при появлении следующих признаков:
	</p><ul>
	<li>боли и спазмы внизу живота;</li>
	<li>наличие кровяных выделений;</li>
	<li>повышенная температура тела.</li>
</ul>
<p>Наличие одного из таких симптомов свидетельствует о развитии воспалительного процесса, поэтому стоит незамедлительно обратиться к гинекологу, который назначит оптимальный курс лечения.
	</p>
<h2>Диагностика и лечение децидуального полипа при беременности 
	</h2>
<p>Диагностируется наличие децидуального полипа методом кольпоскопии. Такая процедура занимает несколько минут и является абсолютно безболезненной. Также в процессе обследования выполняется ряд общеклинических и бактериологических лабораторных исследований. При необходимости выполняется УЗИ органов малого таза.</p>
<p>Лечение децидуального полипа при беременности осуществляется только тогда, когда новообразования начинают кровоточить. В этом случае проводится удаление полипов методом лазерной терапии с последующей термо- и криокоагуляцией образования.</p>
