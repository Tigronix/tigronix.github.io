<h2>Симптомы дисплазии шейки матки</h2>

<p>Сама по себе дисплазия протекает практически бессимптомно. Признаки заболевания проявляются только после присоединения микробной инфекции. В запущенных состояниях симптомами дисплазии шейки матки являются: 
	</p>
<ul>
	<li>боли в нижней части живота;</li>
	<li>необильные влагалищные кровяные выделения;</li>
	<li>жжение, зуд в области половых органов;</li>
	<li>прожилки крови во влагалищных выделениях после полового акта;</li>
	<li>болезненность при сексуальных контактах.</li>
</ul>
<p>На ранних стадиях заболевание диагностируется только во время профилактических гинекологических осмотров. Чтобы избежать появления неприятных симптомов и вовремя начать лечение дисплазии шейки матки, не стоит откладывать визит к врачу.</p>
	
