<h2>Лечение эндометриоза на разных стадиях заболевания: внутренний и внешний эндометриоз 
	</h2>
<p>Главная особенность эндометриоза матки заключается в способности заболевания метастазировать – распространять эндометриоидные клетки по лимфатическим и кровеносным сосудам в различные органы мочеполовой системы и ткани. В связи с этим различают внутренний и наружный эндометриоз. При внутренней форме заболевания болезнь поражает тело матки и маточных труб. В случае развития наружного эндометриоза болезнь поражает не только половые, но и другие органы. 
	</p>
<h2>Стадии эндометриоза
	</h2>
<p>На начальной стадии заболевание проявляется очень редко, так как вначале оно не имеет явных признаков. Диагностируют следующие степени эндометриоза:
	</p><ul>
	<li>внутренний эндометриоз 1 степени (характеризуется поверхностными симптомами: болевыми ощущениями, зависящими от места локализации заболевания);</li>
	<li>внутренний эндометриоз 2 степени (эндометрий врастает в ткани, расширяется территориально и болевые ощущения становятся более выраженными);</li>
	<li>эндометриоз 3 степени (на этой стадии эндометриоз поражает не только матку, но и другие органы, проявляется в виде кист и спаек в маточных трубах и на брюшине);</li>
	<li>эндометриоз 4 степени (выражается появлением глубоких очагов, поражением влагалища и прямой кишки, крупными кистами, сращением органов).</li>
</ul>
<h2>Лечение эндометриоза в Москве 
	</h2>
<p>Стоит знать, что внутренний эндометриоз 1 и 2 степени лучше поддается лечению, поэтому не стоит пренебрегать профилактическими осмотрами у гинеколога, которые необходмо проходить 1-2 раза в год. Лечение эндометриоза всегда осуществляется комплексно и включает в себя: оперативное удаление очагов заболевания методом лапароскопии, гормональную терапию, противовоспалительное и обезболивающее лечение, восстановление иммунной системы, восстановление менструального цикла.
	</p>
<p>Врачи Медклиники «Логон» на Авиамоторной используют современные методики лечения, которые позволяют им успешно справлять с таким недугом как эндометриоз матки. Многолетняя практика и доскональные знания позволяют нашим специалистам гарантировать высокий процент выздоровления. Получить консультацию у врача-гинеколога нашего центра по поводу диагностики и лечения эндометриоза матки можно, записавшись на прием через интернет или по телефону +7 (495) 256-38-00.
