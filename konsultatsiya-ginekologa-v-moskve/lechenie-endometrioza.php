		<h2>Симптомы эндометриоза матки</h2>

<p>Примерно у 20% пациенток отсутствуют клинические признаки заболевания. Диагностировать его своевременно позволяет осмотр у опытного гинеколога. При прогрессировании эндометриоза матки поводом обратиться к врачу являются следующие симптомы:
	</p>
<ul>
	<li>ноющие боли внизу живота или в паху за 1-5 дней до и во время менструации;</li>
	<li>мажущиеся выделения в течение 1-3 дней до и/или после менструации;</li>
	<li>дискомфорт и боли при половом акте;</li>
	<li>вздутие живота;</li>
	<li>обильные менструальные циклы, приводящие к анемии.</li>
</ul>
