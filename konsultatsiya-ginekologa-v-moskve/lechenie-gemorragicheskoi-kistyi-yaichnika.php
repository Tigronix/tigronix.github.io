<h2>Симптомы геморрагической кисты яичника
	</h2>
<p>При перерождении функциональной кисты в геморрагическую у пациенток наблюдается ряд признаков, характерных для этого типа кистоза:</p>
<ul>
	<li>тянущие боли в нижней области живота;</li>
	<li>болезненный менструальный цикл;</li>
	<li>обильные выделения в период менструации;</li>
	<li>болевые ощущения во время полового акта;</li>
	<li>чувство тяжести внизу живота и в области паха;</li>
	<li>нарушение графика менструального цикла.</li>
</ul>

<p>В некоторых случаях пациентки жалуются на приступы тошноты и позывы к рвоте. Иногда при мочеиспускании могут возникать болевые ощущения.</p>
<p>При развитии этого типа кистозного образования существует огромный риск его разрыва, поэтому лечение геморрагической кисты яичника следует начинать как можно раньше.</p>

