<h2>Симптомы гормональных нарушений
		</h2>
<p>При гормональном дисбалансе степень выраженности симптомов зависит от возраста. У девочек-подростков нарушение гормонального фона проявляется задержкой полового развития, ожирением или дистрофией, отсутствием или нестабильностью менструального цикла, частыми головными болями, быстрой утомляемостью. Гормональный дисбаланс у женщин репродуктивного возраста проявляется нарушением менструального цикла, дискомфортом во время полового акта, повышенным ростом волос на теле, синдромом хронической усталости. При климаксе симптомами гормонального дисбаланса являются интенсивное потоотделение, нарушение сердечной деятельности, беспричинная тревога, сильная утомляемость, развитие диабета. 
	</p>
