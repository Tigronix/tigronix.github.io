<h2>Симптомы синдрома хронической тазовой боли у женщин
		</h2>
<p>Характерными признаками такого заболевания являются:
	</p><ul>
	<li>тупые или ноющие боли в пояснице, в нижней области живота, в паху, усиливающиеся при переохлаждении, физической или эмоциональной нагрузке, а также при воспалительных заболеваниях;</li>
	<li>болезненные ощущения в период менструального цикла;</li>
	<li>боли при половом акте.</li>
</ul>
<p>Длительная болезненность при несвоевременном лечении приводит к неблагоприятным последствиям: психоэмоциональному дискомфорту, ухудшению функционирования половых органов, личностной апатии. 
	</p>
