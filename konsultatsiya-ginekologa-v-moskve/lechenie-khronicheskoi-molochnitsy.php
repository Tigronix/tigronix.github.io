<h2>Симптомы хронической молочницы
		</h2>
<p>Главное отличие хронической формы молочницы от обычной заключается в проявлении ежемесячных рецидивов, которые происходят на фоне ослабления иммунитета. Важно также знать, что признаки хронической стадии этого заболевания очень похожи на симптомы обычной формы. Это жжение, неприятные выделения, боли в области половых органов, сильный зуд. Разница только в том, что при хроническом кандидозе симптомы сильнее выражены. Также при такой форме заболевания у женщин наблюдается появление сыпи и трещинок в области половых органов.
	</p>
<h2>Можно ли вылечить хроническую форму молочницы?
	</h2>
<p>Современные методики и препараты позволяют избавиться от такого недуга. При этом лечение хронической молочницы направляется на восстановление нормальной микрофлоры препаратами антигрибкового воздействия.</p>
<p>Лечить хроническую молочницу самостоятельно не рекомендуется, поскольку при неправильном подборе препарата заболевание грозит обострением.</p>
