<h2>Разновидности кисты яичника </h2>

<ul>
	<li>Фолликулярная киста (возникает из ткани фолликула). Новообразование является небольшим по размеру и имеет тонкие стенки. Такая киста разрастается наружу.</li>
	<li>Киста желтого тела. Заполненная жидкостью опухоль с плотными, толстыми стенками. Образуется после овуляции из-за избытка жидкости в желтом теле.</li>
	<li>Эндометриоидные кисты. Это новообразования, заполненные густой кровью и разрастающиеся внутрь слоя стенки матки.</li>
	<li>Кистозные опухоли (возникают из разрастания тканей яичников). К такому типу относятся дермоидные кисты и цистоаденомы.</li>
</ul>

