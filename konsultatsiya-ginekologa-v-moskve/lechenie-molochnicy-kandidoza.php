<h2>Симптомы молочницы у женщин 
		</h2>
<p>Признаками кандидоза у женщин являются следующие симптомы:
	</p><ul>
	<li>покраснение слизистой половых органов и влагалища;</li>
	<li>жжение, зуд, дискомфорт в области половых органов;</li>
	<li>появление сыпи;</li>
	<li>наличие белых, творожистых выделений из влагалища;</li>
	<li>болезненность мочеиспускания;</li>
	<li>болезненные ощущения при половом акте.</li>
</ul>
<p>При наличии перечисленных симптомов следует обязательно обратиться к гинекологу. Лечение молочницы необходимо проводить комплексно. Также важно проводить лечение совместно с половым партнером, чтобы предотвратить повторное развитие болезни.
	</p>
