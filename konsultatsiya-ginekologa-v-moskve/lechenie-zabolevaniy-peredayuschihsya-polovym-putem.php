<h2>Формы ЗППП</h2>

<p>Заболевания, передающиеся половым путем, у женщин, как и у мужчин, протекают в двух формах. При острой форме заболевания первые симптомы появляются уже на 2-7 день после заражения. Если не лечить острую форму ЗППП, то через некоторое время она переходит в хроническую. При хронической форме заболевания симптомы недуга менее выражены, но инфекция, присутствующая в организме, представляет опасность как для самого носителя, так и для близких людей, ведь она передается не только половым путем, но и при использовании чужих предметов гигиены. Наиболее часто у женщин встречаются трихомониаз, хламидиоз, кандидоз, микоплазмоз, гонорея и другие заболевания такого типа. </p>

