<h2>Преимущества медикаментозного аборта:</h2>

<img class="img_center" style="width: 500px; height: 334px; margin: 10px;" src="../../img/zabolel07.jpg" alt="медикаментозный аборт" />

<ul class="list">
	<li>не требует госпитализации и это дает возможность сохранить сам факт беременности и медикаментозного аборта</li>
	<li>процедура медикаментозного аборта совсем безболезненна</li>
	<li>такой аборт психологически переносится легче, нежели хирургический</li>
	<li>нет риска, того, что при процедуре вам занесут инфекцию, травмируют какие–либо половые органы</li>
	<li>медикаментозный аборт имеет более мягкое воздействие на организм по сравнению с хирургическим.</li>
</ul>

<h2>Недостатки медикаментозных абортов:</h2>

<ul class="list">
	<li>прерывание беременности медикаментозным путем возможно только на ранних сроках, до 6 недель</li>
	<li>болезненные маточные сокращения после приема препарата</li>
	<li>многоразовое посещение доктора для контроля абортивного процесса</li>
	<li>длительные кровотечения до 15 дней</li>
</ul>

<p>Несмотря на имеющиеся недостатки, многие женщины отдают предпочтение именно медикаментозному аборту.</p>

<p>Основным действующим веществом абортивных препаратов является Мифепристон. Мифепристон – это стероидный синтетический антигестагеный препарат, который понижает действие прогестерона (главного гормона беременности). Этот препарат прошел много проверок и является абсолютно безопасным препаратом для здоровья женщины. При приеме препарата с этим веществом эндометрий перестает поддерживать развитие эмбриона, шейка матки размягчается и расширяется, что и способствует абортивному процессу. В большинстве случаев после приема препарата беременность прерывается в течение первых шести недель после зачатия путем вызывания искусственного выкидыша.</p>

<h2>Бывают случаи, когда без хирургического вмешательства никак не обойтись:</h2>

<ul class="list">
	<li>когда эмбрион не полностью удален</li>
	<li>когда не проходит длительное и сильное кровотечение</li>
	<li>когда даже максимальная доза препарата не помогла с прерыванием беременности</li>
</ul>

<p>Некоторые правила, которые необходимо соблюдать для успешного проведения процедуры:</p>

<ul class="list">
	<li>проведение этой процедуры помогает только впервые 6 недель беременности</li>
	<li>прохождение ультразвукового исследования на наличие внематочной беременности, так как препарат действует только на маточную беременность</li>
	<li>отсутствие таких заболеваний, как: гипертония, эпилепсия, сердечно-сосудистые патологии</li>
	<li>препарат будет неэффективен при активном курении женщины в это период и приеме препаратов-антикоагулянтов</li>
</ul>

<p>Также после такого способа аборта возможны такие осложнения, как:</p>

<ul class="list">
	<li>слабость, рвота, тошнота, расстройства желудка</li>
	<li>обильные кровянистые выделения</li>
	<li>боли в низу живота</li>
	<li>повышение температуры тела</li>
</ul>

<p>Все эти симптомы имеют место быть при нормальном развитии такой процедуры. Если же боли очень сильные и обильное кровотечение, то необходимо обратиться к доктору.</p>

<p>В случае отсутствия результата после приема препарата у многих женщин возникает вопрос, влияет ли этот препарат на плод, в случае сохранения беременности. Точного ответа на этот вопрос нет, но в любом случае шансы на патологии развития плода при приеме таких препаратов намного выше, чем при нормально протекающей беременности.</p>

<p>Медикаментозные аборты не влияют на детородную функцию, то есть после этого аборта вы можете не бояться, что у вас больше никогда не будет детей. Менструальный цикл восстанавливается уже в первый цикл, поэтому вам необходимо позаботится о надежной контрацепции.</p>

<p>Если у вас возникли какие-либо вопросы, обращайтесь к нам, в наш медицинский центр на Авиамоторной в Москве. Мы ответим и поможем вам с вашей проблемой.</p>

	        	
		
