<h3>Нарушения менструального цикла</h3>

<p>Если у женщины имеются гормональные нарушения, то об этом могут свидетельствовать и наличие нарушений менструального цикла. Это может проявляться в виде отсутствия менструаций, или нарушении выделений - очень скудные, чрезмерно обильные, болезненные или кратковременные.</p>

<p>Чтобы определить наличие нарушений менструального цикла должна быть учтена его продолжительность, регулярность и общее самочувствие женщины. Нормальный цикл составляет от 21 до 35 дней, сама менструация колеблется от 2 до 7 дней. Очень важна регулярность, как цикла, так и длительности менструации. Если есть колебания, то это свидетельствует о наличии проблем со здоровьем, которые могут проявляться и в нарушении гормонального фона.</p>

<p>Также о гормональных нарушениях может говорить болезненность менструаций и сопутствующие симптомы в виде резкого повышения или понижения артериального давления, головокружения, вздутие живота, отечность, упадок сил, общее недомогание.</p>

