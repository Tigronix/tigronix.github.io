<h2>Ведение беременности. План:</h2>

<p><span class="bold">Ведение беременности (контракт) - посещение акушера-гинеколога:</span></p>

<p>до 20 недели – 1 раз в 3 нед.;<br />
20 - 33 недели – 1 раз в 2 нед.;<br />
34  - 39 недели – 1 раз в нед.</p>

<p>Ультразвуковое исследование до 38 нед. проводят 4 – 6 раз.<br />
Ультразвуковое исследование внутренних органов – по показаниям.<br />
ОАК<br />
ОАМ<br />
Биохимический анализ крови.<br />
Анализ крови на сифилис, ВИЧ, гепатиты B,C – 2 раза.<br />
Анализ на группу крови и резус-фактор – 1 раз (если резус-фактор отрицательный, проводятся анализы на антитела по показаниям  5-6раз).<br />
Анализ влагалищного мазка 3 – 4 раза.<br />
ДНК-диагностика мазка  3 – 4 раза.<br />
Скрининговое обследование на отклонения в развитии плода 2 раза.<br />
Исследование системы гемостаза  1 раз.<br />
Анализ крови на внутриутробные инфекции (токсикоплазмоз, краснуху)   1 – 2 раза.</p>

<p><span class="bold">Ведение беременности - лечебные мероприятия</span></p>

<p>Санация влагалища перед родами (36 – 38 неделя).<br />
Назначение витаминов во время всего срока беременности.<br />
В случае выявления отклонений в течении беременности (внутриутробная инфекция, скрытая инфекция, ЖДА и т.д.) – лечение в течение всего срока (лекарства и необходимые доп. процедуры, оплачивающиеся отдельно).<br />
Психопрофилактическая подготовка к родам во время  беременности (школа для беременных).</p>

<p><span class="bold">Наблюдение беременности -  наблюдение</span> <span class="bold">после родов</span></p>

<p>Ультразвуковое исследование матки, придатков, молочных желез.<br />
Анализ влагалищного мазка.<br />
Решение проблемы лактостаза.</p>

