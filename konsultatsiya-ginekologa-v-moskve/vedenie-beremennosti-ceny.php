<p><span class="bold">3 – 5 неделя</span></p>

<p>-  сбор анамнеза,<br />
-    осмотр, антропометрия,<br />
-    измерение веса, артериального давления,<br />
-    Ультразвуковое исследование,<br />
-    взятие анализов,<br />
-    рекомендации по режиму дня и питанию.</p>

