<h2>Причины развития железисто-фиброзного полипа эндометрия
		</h2>
<p>Основными факторами, повышающими риск образования железисто-фиброзного полипа эндометрия, являются:
	</p><ul>
	<li>заболевания яичников;</li>
	<li>нарушение работы надпочечников;</li>
	<li>пониженный иммунитет;</li>
	<li>стрессы, эмоциональное перенапряжение;</li>
	<li>воспалительные заболевания матки;</li>
	<li>заболевания, передающиеся половым путем;</li>
	<li>ожирение, сахарный диабет, гипертоническая болезнь;</li>
	<li>выскабливания, аборты и другие внутриматочные манипуляции;</li>
	<li>нарушение гормональной функции.</li>
</ul>
<h2>Симптомы железисто-фиброзного полипа эндометрия
	</h2>
<p>На начальной стадии это заболевание протекает практически бессимптомно. В процессе роста полипов у женщин наблюдаются следующие симптомы:
	</p><ul>
	<li>наличие кровяных выделений вне графика менструации;</li>
	<li>болевые ощущения во время полового акта;</li>
	<li>обильные менструальные циклы;</li>
	<li>невозможность зачатия ребенка;</li>
	<li>мажущиеся выделения во время менопаузы.</li>
</ul>
