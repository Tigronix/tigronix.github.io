<h2>Гормоны и гормональный фон</h2>

<p>Гормоны вырабатываются в организме женщины во многих органах и при этом каждый гормон отвечает за ту или иную функцию женского организма. Гормоны вырабатывают:</p>

<ul class="list">
	<li>щитовидная железа и паращитовидные железы;</li>
	<li>надпочечники;</li>
	<li>яичники;</li>
	<li>поджелудочная железа;</li>
	<li>гипоталамус;</li>
	<li>гипофиз;</li>
	<li>вилочковая железа;</li>
	<li>и т.д.</li>
</ul>

<p>После того, как железами были выделены гормоны, они разносятся с кровью к различным органам и оказывают на них воздействие, что влияет на дальнейшее функционирование всего организма в целом.</p>

<p>В женском организме существует более 60 гормонов, баланс между содержанием которых и называется гормональным фоном. Но надо отметить, что это баланс весьма хрупки и очень часто бывает подвержен влиянию различных факторов, к которым относятся:</p>

<ul class="list">
	<li>нарушение веса;</li>
	<li>возраст;</li>
	<li>нарушение пищеварения;</li>
	<li>нарушения сна;</li>
	<li>вирусные заболевания;</li>
	<li>опухоли;</li>
	<li>инфаркты;</li>
	<li>наследственность;</li>
	<li>длительный стресс;</li>
	<li>лекарственные препараты;</li>
	<li>аутоиммунные процессы;</li>
	<li>курение и употребление наркотиков;</li>
	<li>паразиты.</li>
</ul>

<p>Именно дисбаланс гормонов в женском организме чаще всего приводит к ряду гинекологических заболеваний и патологических состояний. Кроме того, при гормональном сбое меняется эмоциональное и физическое самочувствие.</p>

<p>В норме, в организме женщины должны одновременно функционировать как женские, так и мужские гормоны, которые должны находиться в сбалансированном состоянии. Но при дисбалансе половых гормонов начинают происходить нарушения.</p>

