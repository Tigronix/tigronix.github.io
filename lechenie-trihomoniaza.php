<h2>Лечение трихомониаза</h2>

<p><img class="img_center" style="width: 460px; height: 373px; margin: 10px;" src="../img/trihomonada.jpg" alt="лечение трихомониаза" /></p>

<div class="clr"></div>

<p align="center"><span class="bold">В «Клинике на Авиамоторной» проводят полный комплекс лечения трихомониаза и профилактики трихомониаза</span></p>

<h2>Симптомы</h2>

<p><span class="bold">У мужчин</span>. При первичном заражении Trichomonas vaginalis (трихомониаз) у пациентов развивается уретрит. Если заболевание протекает в острой форме, характерными симптомами являются обильные выделения гноя из мочеиспускательного канала, а также боль при опорожнении мочевого пузыря. Мужчины, у которых заболевание в хронической форме, отмечают небольшое количество сероватых выделений. При трихомонадоносительстве (заболевание в торпидной форме) симптомы могут полностью отсутствовать.</p>

<p><span class="bold">У женщин</span>. Заболевание сопровождается желтыми пенящимися влагалищными выделениями, болью при половых контактах и мочеиспускании, покраснением и припухлостью наружных половых органов. Если заболевание в хронической форме, то во время диагностики может быть выявлена гиперемия шейки матки и свода влагалища. Часто заболевание вызывает симптомы вульвовагинита.</p>

<p>Важно начать профилактику трихомониаза своевременно до появления осложнений и провести комплекс лечения трихомониаза.</p>

<h2>Методы диагностики и лечения трихомониаза</h2>

<p><span class="bold">Мазок нативного препарата</span>. Исследование проводится сразу же после получения материала. Наличие Trichomonas vaginalis определяется по форме (грушевидная или овальная) и характерному движению паразита.</p>

<p><img style="width: 350px; height: 300px; margin: 10px; float: left;" src="../img/482x351_skrutye_infektsii.jpg" alt="лечение трихомониаза - анализы" /><img style="width: 350px; height: 300px; margin: 10px; float: left;" src="../img/152-vaginal.jpg" alt="трихомонады" /></p>

<p><span class="bold">Микроскопия окрашенного препарата</span>. Диагностика может проводиться через некоторое время после забора мазка. Для определения заражения используется 1% раствор мителенового синего или окрашивание по Граму.</p>

<p><span class="bold">Микробиологическое исследование</span>. Этот метод диагностики эффективен сразу же после заражения и на любой стадии заболевания трихомониаз. Полимеразная цепная реакция позволяет получить наиболее точные данные о наличии/отсутствии в препарате Trichomonas vaginalis и оптимизировать курс лечения трихомониаза.</p>

<p><span class="bold">Культуральное исследование</span>. Эффективно для определения инфекции при заболевании в хронической и скрытой форме. Для обнаружения Trichomonas vaginalis использует простые методы посева на питательные среды.</p>

<p><span class="bold">Латекс-агглютинация</span>. Используется, если необходима диагностика в торпидной торпидной форме. Биологический материал смешивается с физраствором и латексным реагентом. При положительной реакции происходит образование комочков латекса.</p>

<h2>Схема профилактики трихомониаза и лечения трихомониаза</h2>

<p>Профилактика трихомониаза - инфекция может иметь несколько очагов распространения, поэтому профилактика трихомониаза является комплексной. Курс лечения трихомониаза подбирается врачом индивидуально для каждого пациента только после осмотра и подтверждения диагноза с учетом стадии и характера заболевания. Лечение должны пройти все половые партнеры больного, зараженного Trichomonas vaginalis. Медикаментозное лечение трихомониаза предполагает прием противомикробных препаратов. Курс лечения трихомониаза и схема приема лекарств для профилактики заболевания определяется врачом. В качестве дополнительных методов лечения применяются физиотерапивтические процедуры и иммунокоррегирующую терапию. Лечение пациента осуществляется общеукрепляющими препаратами, иммуномодуляторами, поливитаминами при профилактике трихомониаза.</p>

<h2>Осложнения при заболевание</h2>

<p>При отсутствии лечения инфекция может вызывать:</p>

<ul class="list">
	<li><span class="bold">бартолинит</span>. Воспаление железы, расположенной у входа во влагалище, сопровождающееся опухолью половых губ, болью, повышением температуры при запущенном заболевании.</li>
	<li><span class="bold">цистит</span>. Воспаление мочевого пузыря, которое сопровождается резкой болью при мочеиспускании, болями в нижней части живота, учащенным мочеиспусканием.</li>
	<li><span class="bold">скинеит</span>. Воспаление железы Скина, расположенной на задней стенке уретры у женщин, сопровождающееся болями при мочеиспускании и цистоподобными симптомами после полового акта.</li>
	<li><span class="bold">простатит</span>. Воспаление предстательной железы, которое сопровождается болями в промежности, нарушениями половой функции, учащенным мочеиспусканием.</li>
</ul>

