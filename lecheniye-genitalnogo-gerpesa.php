<p><span class="bold">Самолечение генитального герпеса дает временный эффект улучшения состояния и в итоге приводит к хроническим процессам </span></p>

<p>Генитальным герпесом называется заболевание половых органов, вызываемое вирусами ВГП или Herpes simplex. К сегодняшнему дню известны 8 типов генитального герпеса. В 80% случаев болезнь вызывается ВГП-2, в остальных – ВГП-1 или их сочетанием. Вирус поражает наружные половые органы, влагалище, шейку матки, промежности и область заднего прохода, может переходить на яичники, маточные трубы и тело матки.</p>

<p><img class="img_center" style="width: 650px; height: 473px; margin: 10px;" src="../img/projavlenija-i-lechenie-gerpesa_1.jpg" alt="лечение генитального герпеса" /></p>

<div class="clr"></div>

<p>При отсутствии своевременного комплексного лечения генитального герпеса, болезнь начинает прогрессировать, активно размножаться, поражая кожу и слизистые оболочки. Генитальный герпес является одним из возбудителей воспалительных заболеваний мочеполовой системы, желудочно-кишечного тракта, сосудисто-кровяной и даже нервной систем человеческого организма. У беременных женщин герпес может стать причиной выкидыша, инфицирования или патологии плода. Недавние исследования доказали связь между генитальным герпесом и раком шейки матки и предстательной железы.</p>

<p>Настоятельно не рекомендуем заниматься самолечением генитального герпеса – это может привести к временному или частичному улучшению состояния, но в итоге приведет к развитию хронической формы болезни. Для лечения генитального герпеса необходимо обратиться к специалисту!</p>

<p><span class="bold">Проявления генитального герпеса</span></p>

<ul class="spisok">
	<li>Появление в промежности, на поверхности бедер, вокруг заднего прохода и на внешних половых органах мелких пузырьков, наполненных мутной жидкостью.</li>
	<li>Зуд, покраснение кожи вокруг этих пузырьков.</li>
	<li>Образование эрозий и язвочек на месте лопнувших пузырьков. Поврежденная кожа постепенно слущивается и шелушится, заменяясь здоровой.</li>
	<li>Увеличение паховых лимфоузлов.</li>
	<li>Общее недомогание, физическая усталость, боли в мышцах и повышение температуры.</li>
	<li>Дискомфорт, зуд и покалывание при мочеиспускании (в случае поражения мочеиспускательных каналов).</li>
</ul>

<p>Все эти симптомы требуют незамедлительного лечения генитального герпеса!</p>

<p><span class="bold">Методы лечения</span></p>

<p>При лечении генитального герпеса используются противовирусные препараты. Комплексное лечение противовирусными препаратами направлено на ускорение процесса редукции высыпаний, уменьшение объемов выделений и снижения рисков рецидива болезни. Врач назначает курс лечения и препараты в индивидуальном порядке, в зависимости от формы герпеса, его выраженности и длительности заражения генитальным герпесом. Лечащий врач строго контролирует весь процесс лечения, в случаях возникновения осложнений или тяжелого течения заболевания генитальным герпесом пациента могут поместить на лечение в стационар.</p>

<p><span class="bold">Лечение иммуномодулирующими препаратами.</span> Для лечения пациенту назначается курс биогенных стимуляторов, иммуномодуляторов, адаптогенов, интерферона и его индукторов. Этот метод лечения направлен на восстановление и активизацию работы иммунной системы зараженного вирусом человека. Выпускаются в формах для наружного и перорального применения. Для лечения в продромальном периоде течения болезни, когда высыпания еще не начали превращаться в язвочки, используются препараты с наружным применением, также они используются для лечения рецидива герпеса до появления эрозии.</p>

<p><span class="bold">Герпетическая вакцинация.</span> Эта процедура назначается для лечения как уже зараженным пациентам, так и неинфицированным лицам в качестве иммунной терапии. Вакцина способствует лечению генитального герпеса, а также выработке организмом естественных вирусонейтрализирующих антител и предотвращает от передачи вируса. Пациентам, склонным к частым рецидивам заболевания, вакцинация проводится по окончанию курса лечения иммуномодулянтами.</p>

<p><img style="width: 250px; height: 188px; margin: 10px; float: left;" src="../img/gerpes-lazernoe-obluchenie-krovi.jpg" alt="лазерное облучение крови при генитальном герпесе" /><span class="bold">Лазерное облучение крови.</span> Суть метода заключается в интра- и экстракорпоральном воздействии на плазму крови квантами оптического излучения. Интракорпоральное облучение подразумевает непосредственное введение облучителя в вену пациента или надвенное облучение, когда источник облучения размещается над кровеносными сосудами больного. При экстракорпоральном облучении кровь пациента забирается для обработки в специальных магистралях, после чего переливается обратно. Такое лечение активизирует работу иммунной системы и оказывает противовоспалительное действие.</p>

