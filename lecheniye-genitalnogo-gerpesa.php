<p><img style="width: 250px; height: 375px; margin: 10px; float: left;" src="../img/plazmoforez-pri-gerpese.jpg" alt="плазмофорез при генитальном герпесе" /></p>



<p><span class="bold">Процедура плазмафереза.</span> Процедура представляет собой удаление из крови пациента плазмы, содержащей в себе токсические и метаболические элементы. Из кровеносной системы больного герпесом пациента забирается порция крови, ее помещают в систему и разделяют на форменные элементы и плазму. После этого клетки возвращают обратно в организм, а плазму утилизируют. Цель такого лечения – детоксикация организма.</p>



