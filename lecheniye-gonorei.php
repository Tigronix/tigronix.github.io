<h2>Способы заражения</h2>

<p><span class="bold">Половым путем.</span> Главным образом, источником заражения являются больные, имеющие хроническую гонорею. К данной группе преимущественно относятся женщины, так как у них заболевание протекает более длительно, почти бессимптомно и трудно диагностируется. Также гонорею может передать источник, являющийся бактерионосителем.</p>

<p><span class="bold">Внеполовым путем.</span> В ряде случаев заболевание можно получить внеполовым путем через полотенца, губки и белье, на которых сохранился невысохший гонорейный гной. Инфицирование новорожденного происходит во время родов при прохождении плода по родовым путям больной матери.</p>

<h2>Методы лечения</h2>

<p><span class="bold">Противовирусная терапия.</span> Лечение гонореи включает антибиотикотерапию. Прежде, чем приступить к лечению, врач должен назначить сдачу анализов на чувствительность гонококков к антибиотикам. По результатам исследований выбирают препараты, которые является оптимальными для конкретного пациента. Как правило, применяются лекарственные средства из группы пенициллинов и цефалоспоринов. На данном этапе лечения проводят диагностику хламидиоза, даже при отсутствии симптомов, так как часто эти инфекции соседствуют. При получении положительного результата к терапии добавляются антихламидийные препараты.</p>

<p><span class="bold">Противомикробная терапия.</span> В качестве местного лечения проводят инстилляцию антибактериальных препаратов во влагалище или уретру, назначают обезболивающие свечи и микроклизмы. С целью повышения эффективности терапии антибиотики необходимо сочетать с приемом сульфаниламидов. Препараты группы сульфаниламидов также назначаются при отсутствии положительных результатов антибиотикотерапии, наличии непереносимости антибиотиков или для лечения смешанной инфекции.</p>

<p><span class="bold">Специфическая иммунотерапия.</span> Иммунотерапия – это вспомогательный метод излечения гонореи, который используется наряду с другими видами терапии для повышения реактивности организма в борьбе с инфекцией. Гонококковая вакцина показана пациентам после безрезультативной антибиотикотерапии при вялотекущих рецидивах, при хронических и свежих торпидных формах гонореи, а также больным с восходящей инфекцией после стихания острых воспалительных явлений. Вакцинотерапия в амбулаторных условиях проводится одновременно с приемом антибиотиков. В стационарных условиях антибиотики принимаются во время либо по окончании вакцинотерапии. Вакцину для лечения инфекции вводят внутрикожно или внутримышечно в области ягодиц. Женщинам с хронической и торпидной формой заболевания назначается очаговая вакцинация в подслизистую ткань уретры и шейки матки. Реакция на вакцинацию может быть температурной, общей (слабость, головная боль, недомогание), местной (болезненность в районе инъекции) и очаговой (помутнение мочи, болезненность в пораженных органах, усиление выделений).</p>

<p><span class="bold">Внимание!</span> Во время лечения инфекции запрещено употребление спиртных напитков. Следует воздержаться от половых связей. Ежедневно обязательно показа смена нижнего белья и туалет половых органов теплой водой с мылом. В случае приема антибиотиков тетрациклинового ряда необходимо избегать долгого пребывания на солнце, так как данные препараты усиливают чувствительность кожи к УФ-излучению.</p>

<p style="text-align: justify;">Болезнь следует контролировать после лечения.</p>

<p>Контроль лечебного курса от гонореи у женщин и мужчин осуществляется через 7-10 дней по окончании прохождения курса антибиотиков. После провокации уколом пирогенала или гоновакцины у пациентов берутся мазки для микроскопического и культурального исследования. Затем алгоритм повторяется у женщин в течение 2-3 менструальных циклов, у мужчин – через 2-3 недели.</p>

<p style="text-align: justify;">Осложнения заболевания</p>

<p><span class="bold">У мужчин</span> при отсутствии своевременного лечебного вмешательства инфекция гонореи может вызвать следующие осложнения:</p>

<ul class="spisok">
	<li>простатит;</li>
	<li>различные воспалительные заболевания полового члена и яичек;</li>
	<li>половая дисфункция;</li>
	<li>бесплодие.</li>
</ul>

