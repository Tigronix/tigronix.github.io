<h2>Возбудитель гонореи</h2>

<p><span class="bold">Гонорея</span> является инфекционным заболеванием. Гонорею вызывает специфический возбудитель – гонококк диплококк Neisseria gonorrhoeae. Гонококк имеет форму боба, сложенного внутрь вогнутыми сторонами. Гонорея неустойчива во внешней среде, но способна развиваться внутри организма. Это становится возможным благодаря защитной капсуле, оберегающей гонококк от воздействия иммуноглобулинов, – веществ, которые защищают организм человека от болезнетворных микробов. Гонорея преимущественно передается половым путем и, в основном, характеризуется поражением слизистых оболочек органов мочеполовой системы. В ряде случае наблюдаются поражения прямой кишки и полости рта. Особенностью возбудителя гонореи является образование бета-лактомазы, которая ослабляет действие антибиотиков, принимаемых больным. Чаще всего бета-лактамные штаммы ответственны за многочисленные осложнения и хроническое течение гонореи.</p>

