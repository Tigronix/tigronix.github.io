<p><span class="bold">Специалисты «Клиники на Авиамоторной» предлагают профессиональные медицинские услуги в области эффективного лечения кандидоза</span></p>

<p><span class="bold">Кандидоз</span> является одной из разновидностей грибковых инфекций. Кандидоз вызывают дрожжеподобные микроскопические грибы рода Candida (Candida albicans). Кандидоз обусловлен не просто наличием данных грибов, а их активным размножением, и/или появлением более патогенных штаммов. Как правило, кандидоз возникает в результате снижения местного или общего иммунитета. У женщин воспалительные изменения при кандидозе могут ограничиваться влагалищем (вагинит) или наружными половыми органами (вульвит), но обычно наблюдается комплексное поражение (вульвовагинит). Кандидоз обычно приводит к появлению уретрита (воспаления мочеиспускательного канала) и цистита (воспаления мочевого пузыря). У мужчин кандидоз может спровоцировать не только уретрит и цистит, но и простатит (воспаление предстательной железы), а также баланопостит (воспаление крайней плоти).</p>

<h2>Симптомы</h2>

<p><span class="bold">У мужчин</span> характерны следующие симптомы кандидоза:</p>

<ul style="margin-left: 20px;">
	<li>ощущение дискомфорта и зуд в области крайней плоти и головки полового члена основной симптом кандидоза;</li>
	<li>раздражение и жжение при мочеиспускании;</li>
	<li>появление беловатого налета на крайней плоти и головке членаосновной симптом кандидоза;</li>
	<li>покраснение и раздражение крайней плоти и головки члена основной симптом кандидоза;</li>
	<li>преждевременное семяизвержение или появление неприятных ощущений при половом акте основной симптом кандидоза.</li>
</ul>

<p><span class="bold">У женщин</span> характерны следующие симптомы кандидоза:</p>

<ul style="margin-left: 20px;">
	<li>покраснение наружных половых органов и влагалища сведетельствуют о симптомах кандидоза;</li>
	<li>острое жжение и зуд в области половых органов;</li>
	<li>появление творожистых вагинальных выделений белого цвета говарят о симптомах кандидоза;</li>
	<li>нарушение или болезненность мочеиспускания;</li>
	<li>появление болезненных ощущений при половом акте сведетельствуют о симптомах кандидоза.</li>
</ul>

<h2>Методы лечения кандидоза</h2>

<p>Лечение кандидоза должно выполняться комплексно, так как в большинстве случаев кандидоз сопровождается иными венерическими заболеваниями. Например, на фоне кандидоза может развиться цитомегаловирус, герпес, уреаплазмоз, хламидиоз и т.д.</p>

<p><span class="bold">Физиотерапия.</span> Основной задачей физиотерапии является оказание противовоспалительного, бактериостатического, противозудного и обезболивающего действия. В данном случае для лечения инфекции молочницы может использоваться индуктотермия влагалища, дарсонвализация влагалища, магнитотерапия, УФО, микроволновая терапия, цинк-электрофорез, лазеротерапия.</p>

<p><span class="bold">Лечение кандидоза препаратами для местного применения.</span> Местное лечение включает использование кремов, мазей, вагинальных свечей и таблеток, содержащих противогрибковые средства (антимикотики). Обычно для местного лечения молочницы применяются средства на основе клотримазола (группа имидазолы). При помощи специального аппликатора женщины должны вводить вагинальные таблетки глубоко во влагалище. Мужчинам необходимо наносить тонкий слой крема на внутреннюю поверхность крайней плоти и половой член. Препараты против молочницы могут использоваться одно- либо многократно. Схема лечения молочницы строго индивидуальна и назначается врачом только после осмотра пациента и проведения диагностики молочницы.</p>

<p><span class="bold">Лечение кандидоза препаратами для внутреннего применения.</span> Наиболее распространенными противогрибковыми препаратами являются средства на основе флуконазола (группа триазолов). Данные препараты против молочницы выпускаются в суспензиях, таблетках или капсулах для приема внутрь. Лекарственные средства обладают мощным противогрибковым эффектом и избирательно воздействуют на грибковые ферменты. В результате приема повышается проницаемость клеточной мембраны грибов, что приводит к нарушению их роста и размножения. Необходимая доза препаратов против кандидоза может быть назначена только квалифицированным врачом.</p>

<p><span class="bold">Внимание!</span> Часто кандидоз сочетается с неспецифической бактериальной инфекцией, например, стрептококковой, стафилококковой, которая постоянно обитает в микрофлоре половых органов и вызывает заболевание лишь при снижении иммунитета. По этой причине любой вид лечения молочницы должен проводиться только после предварительного осмотра и определения возбудителя инфекции молочницы.</p>

<h2>Кандидоз – сопряженные осложнения</h2>

<p><span class="bold">Внутриутробное заражение плода.</span> Вагинальный кандидоз может возникать во время беременности вследствие быстрых гормональных изменений, которые происходят в организме женщины. При отсутствии терапии заболевания - кандидоз может быть передан новорожденному. Инфицирование плода кандидозом происходит как при протекании беременности, так и во время родов. У новорожденных может проявиться кандидоз полости рта, для которого характерно образование болезненных ран и язвочек в горле и на языке.</p>

<p><span class="bold">Поражение мочевой системы.</span> При отсутствии своевременной и профессиональной терапии заболевания - кандидоз может распространиться на почки и мочевыводящие пути. В данных органах воспалительный процесс протекает длительное время. Периодически кандидоз может обостряться, в ряде случаев присоединяются новые бактериальные инфекции.</p>

<p><span class="bold">Снижение полового влечения.</span> Сексуальные проблемы связаны с появлением жжения, болезненных ощущений во время и после полового акта, которые возникают при отсутствии комплексной терапии инфекции молочницы. Это может стать причиной психологического дискомфорта и как следствие – отсутствия полового влечения.</p>

