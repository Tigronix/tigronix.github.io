<p align="center"><span class="bold">Специалисты «Клиники на Авиамоторной» осуществляют диагностику и профилактику хламидиоза с использованием новейших медицинских методик</span></p>

<p><span class="bold">Хламидиоз</span> является передающимся половым путем инфекционным заболеванием. Возбудитель хламидиоза – микроорганизм Chlamydia trachomatis. Заражение хламидиями может привести к одновременному поражению наружных и внутренних половых органов, сердечно-сосудистой системы, слизистых оболочек дыхательных путей, суставов, зубов, органов слуха и зрения. Хламидиоз в 40% случаев передается от больной матери ребенку во время родов. У мужчин заболевание хламидиозом ведет к прекращению сперматогенеза. Диагностику инфекции затрудняет то, что хламидиоз может протекать бессимптомно.</p>

<h2>Методы профилактики хламидиоза</h2>

<p><span class="bold">Общее медикаментозное</span>. Процедура при профилактике хламидиоза у мужчин и женщин проводится с применением противомикробной терапии. Больным хламидиозом назначаются антибиотики макролидной или тетрациклиновой группы. Схема приема препаратов на основе азитромицина и доксициклина, а также продолжительность курса лечения хламидиоза определяется врачом. Кроме антибиотиков при заболевание пациенту могут быть назначены противогрибковые лекарственные средства и имунностимулирующие препараты на базе интерферона человека. Курс медикаментозной терапии также должен пройти половой партнер больного.</p>

<p><span class="bold">Местное</span>. Комплексная процедура при профилактике хламидиоза  включает проведение процедур с использованием вагинальных тампонов, суппозиториев, гелей, кремов и инстилляций раствором антибактериальных препаратов. Перед введением лекарственных средств влагалище промывается содовым раствором. Продолжительность местной терапии определяется лечащим врачом. При местной профилактике хламидиоза инфицированных органов у мужчин назначаются клизмы, ректальные свечи и инстилляции в мочеиспускательный канал.</p>

<p><span class="bold">Физотерапия</span>. Профилактика хламидиоза  комплексом физиотерапевтических процедур. При заболевание может быть назначен ионо- или электрофарез лекарственных препаратов, квантовая терапия, инфракрасное излучение. Для предотвращения спаечного процесса и нормализации функции яичников при реабилитации применяется ультразвуковая и магнитная терапия, а также электроутеростимуляция. Количество необходимых процедур определяется индивидуально для каждого больного хламидиозом. Продолжительность программы, включающей реабилитационную терапию, в среднем составляет 2-3 месяца.</p>

<p><span class="bold">Осложнения при хламидиозе</span></p>

<p>Несвоевременная диагностика хламидиоза и отсутствие эффективного лечения ведут к прогрессированию заболевания и следующим осложнениям:</p>

<ul class="list">
	<li><span class="bold">Болезнь Рейтера</span>. Инфекция хламидиоза вызывает синдром, симптомами которого являются артрит, конъюнктивит и уретрит. Заболевание вызывает эрозивные поражения кожных покровов. У мужчин болезнь Рейтера может вызывать цирцинарный баланопостит (воспаления и высыпания на половом члене).</li>
	<li><span class="bold">Стриктура уретры</span>. Хронический хламидиоз провоцирует рубцовые изменения слизистой оболочки уретры, что ведет к сужению мочеиспускательного канала. Тяжелая форма стриктуры уретры может привести к полной блокаде оттока мочи. Устранить заболевание можно только с помощью хирургического вмешательства.</li>
	<li><span class="bold">Орхоэпидидимит</span>. Это осложнение при заболевание у мужчин приводит к сужению семявыводящих путей и прекращению нормального сперматогенеза. Орхоэпидидимит является одной из распространенных причин мужского бесплодия.</li>
	<li><span class="bold">Хронический простатит</span>. Инфекция хламидиоза вызывает у мужчин сужение протоков простаты, гибель железистой ткани предстательной железы, изменение количества и качества ее секрета. Хронический простатит ведет к бесплодию.</li>
	<li><span class="bold">Воспаление органов малого таза</span>. Инфекция хламидиоза может провоцировать воспалительные процессы — эндометрит, сальпингоофорит, сальпингит. В результате заболеваний образуются спайки, вызывающие непроходимость маточных труб – одну из основных причин бесплодия.</li>
	<li><span class="bold">Осложнения беременности и родов</span>. Не вылеченное заболевание хламидиоза может вызывать самопроизвольные аборты, тяжелое течение родового процесса, послеродовые осложнения. В процессе диагностики детей, рожденных зараженными матерями, обнаруживается хламидийная пневмония, энцефалопатия, поражения легких, сердца и печени.</li>
</ul>

