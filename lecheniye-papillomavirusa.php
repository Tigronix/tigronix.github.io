<h2>Проявления заболевания ВПЧ</h2>

<p><span class="bold">Бородавки.</span> У больных папилломавирусом часто наблюдаются  бородавки. Они являются доброкачественными новообразованиями кожи, которые представляют собой плотные опухоли диаметром 2-10 мм. Они имеют четкие границы, неровную поверхность и неправильную или округлую форму. Цвет бородавок у больных папилломавирусом варьируется от серовато-черного и бурого до желтого и светло-серого.</p>

<p><span class="bold">Папилломы.</span> Папилломы являются доброкачественными образованиями округлой формы размером 0,2-10 мм. Они имеют широкое основание или ножку. Цвет папиллом при ВПЧ варьируется от светло-коричневого до телесного.</p>

<p><span class="bold">Остроконечные кондиломы.</span> Остроконечные кондиломы являются доброкачественным разрастанием эпидермиса и подлежащего сосочкового слоя размером от 1 мм. Они имеют мягкую структуру и выраженное узкое основание. Цвет остроконечных кондилом - розовый или ярко-красный цвет.</p>

