<h2>Виды диагностики ВПЧ</h2>

<p><span class="bold">Кольпоскопия.</span> В данном случае процесс диагностики заболевания ВПЧ проводится с помощью кольпоскопа – специального микроскопа, позволяющего изучать внешний вид шейки матки. При многократном увеличении становятся видны отдельные сосочки папиллом, в каждом из которых проходит кровеносный петлевидный капилляр. К такому ВПЧ-тестированию прибегают довольно часто.</p>

<p><span class="bold">Биопсия.</span> Биопсия при диагностике заболевания ВПЧ проводится для исключения возможности онкологии. Процедура представляет собой взятие из организма тканей, их окрашивание и последующее исследование. Для диагностики ВПЧ проводят следующие виды биопсии:</p>

<ul class="list">
	<li><span class="bold">Цитологическое исследование ВПЧ.</span> При проведении диагностики изучаются изменения в клетках, вызванные заболеванием. Клетки для цитологического исследования ВПЧ берут с поверхности шейки матки и проводят ВПЧ-тест.</li>
	<li><span class="bold">Гистологическое исследование ВПЧ.</span> При проведении диагностики (впч-тест) изучается строение клеток и тканей, правильность расположения слоев.</li>
</ul>

<p style="text-align: justify;">Методы лечения</p>

<p><span class="bold">Назначение противовирусных препаратов.</span> При лечении пациентам назначаются препараты, которые блокируют размножение вируса и стимулируют работу иммунной системы. Больным ВПЧ необходимо принимать лекарственные средства, способствующие выработке собственных интерферонов. Продолжительность курса и дозы должен определять только квалифицированный врач, согласно диагностике ВПЧ.</p>

<p><span class="bold">Проведение деструктивной терапии ВПЧ.</span> В данном случае терапия проводится с помощью хирургического удаления очага поражения ВПЧ. Деструктивное лечение может быть следующих видов:</p>

<ul class="list">
	<li><span class="bold">Химическая коагуляция.</span> Такая процедура включает воздействие препаратами из органических и неорганических кислот на единичные образования вируса.</li>
	<li><span class="bold">Криодеструкция.</span> Лечение представляет собой обработку единичных образований вируса жидким азотом.</li>
	<li><span class="bold">Электрокоагуляция.</span> Такая процедура лечения представляет собой удаление образований с помощью специального прибора, нагревающегося до высокой температуры под действием тока.</li>
	<li><span class="bold">Лазерная хирургия.</span> Процедура представляет собой воздействие на образование вируса с помощью низкоинтенсивного лазерного излучения.</li>
	<li><span class="bold">Конизация шейки матки.</span> Такая процедура представляет собой удаление с помощью скальпеля конусообразного фрагмента шейки матки.</li>
</ul>

