<p>Бесплодие  бывает двух видов – первичное и вторичное бесплодие.</p>

<p><span class="bold">Первичное</span> выявляется, если была регулярная половая жизнь со здоровым партнером, не предохраняясь,  и ранее не было беременностей. По статистическим данным каждая шестая российская пара сталкивается с проблемой бесплодия.</p>

<p><span class="bold">Вторичным</span> называют бесплодие, если  не получается забеременеть после хотя бы одной беременности, вне зависимости от того , как она завершилась ( роды, аборт, выкидыш).</p>

<p>В любом случае, обращаться к врачу стоит уже через год, после безуспешных попыток забеременеть. Лечению бесплодию необходимо уделить максимум внимания.</p>

<p>Возникновение женского бесплодия может быть следствием многих заболеваний. Давайте рассмотрим <span class="bold">основные причины бесплодия</span>:</p>

<p><img class="img_center" style="width: 600px; height: 438px; margin: 10px;" src="../img/gen_graf05.jpg" alt="лечение женского бесплодия. причины" /></p>

<div class="clr"></div>

<p>Одной из причин женского бесплодия является <span class="bold">поликистоз яичников</span>. Он образуется вследствие гормонального сбоя. Симптомы явны – это повышенный рост волос, нарушенный или полностью отсутствующий менструальный цикл. В таком случае фолликулы недоразвитые, или же зрелая яйцеклетка вообще отсутствует. Как следствие – возникают кисты, вызывающие необходимость лечить бесплодие.</p>

<p>Проблемы бесплодия у женщин могут возникать из-за рубцов, образовавшихся после каких-либо операций, перенесенных инфекционных заболеваний. Это также затрудняет развитие фолликулов. Образовавшиеся спайки окутывают яичник, могут размещаться между яичником и маткой.</p>

<p>Нарушения в гормональном фоне очень сильно влияют на возможность забеременеть. Гормональный дисбаланс может быть последствием патологических заболеваний. Такие нарушения могут вызывать отсутствие менструации, сбой в овуляции. Соответственно, возникают проблемы бесплодия, в этом случае необходимо лечить бесплодие.</p>

<p>Причиной бесплодия бывает неправильное строение матки. Деформированная матка не позволяет яйцеклетке прикрепиться к эндометрию, меняется состав слизи. Такое бывает при миомах, полипах, эррозиях. Как следствие, зачатие не происходит. Перед тем, как начать лечить бесплодие, врач устраняет причину бесплодия, вызвавшую необходимость лечения.</p>

<p>Изменения подвижности маточных труб, а также их полная непроходимость еще одна причина возникновения бесплодия. Такие проблемы возникают после воспалений, после перенесенных родов, аборта или выкидыша. Последствия таких заболеваний могут быть самыми разными.</p>

<p><span class="bold">Эндометриоз</span> возникает, когда клетки эндометрия начинают разрастаться в толще матки и выходят за ее пределы. Между очагами образуются спайки. Яйцеклетка полностью не созревает, не может слиться со сперматозоидом, не позволяет ей прикрепиться к стенке матки.</p>

