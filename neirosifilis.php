<p>В развитии <span class="bold">нейросифилиса</span> основную роль играют отсутствие или недостаточность предшествующего противосифилитического лечения, черепно-мозговая травма, хронические инфекции, интоксикации, иммунные нарушения. Классификация поражений нервной системы при сифилисе несовершенна. С клинической точки зрения целесообразно выделять: сифилис центральной нервной системы, сифилис периферической нервной системы, функциональные нервные и психические явления при сифилисе. В настоящее время для практических целей применяют клинико-морфологи-ческую классификацию.<br />
<br />
Выделяют ранние и поздние формы сифилиса нервной системы. Такое подразделение нейросифилиса условно. Различие между ними основано на характере патологических изменений в нервной системе и хронологически не совпадает с обычной периодизацией сифилиса.</p>

<p><span class="bold">Ранний нейросифилис</span> (до 5 лет от момента заражения) называют мезенхимным, так как поражаются оболочки и сосуды мозга, преобладает мезенхимная реакция.</p>
<p><span class="bold">Поздний нейросифилис</span> (не ранее 6-8 лет после заражения) называют паренхиматозным в связи с поражением нейронов, нервных клеток, а также нейроглии. Изменения носят воспалительно-дистрофический характер, мезенхимная реакция не выражена.</p>

