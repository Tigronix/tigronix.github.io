<p><span class="bold">Скрытый сифилитический клинически бессимптомный менингит</span> развивается в результате раздражения оболочек головного мозга либо непосредственно бледной трепонемой, либо продуктами её жизнедеятельности. Эта форма раннего нейросифилиса названа скрытым ранним менингитом потому, что у большинства больных клинические симптомы менингита обнаружить не удаётся. Некоторые больные могут жаловаться на головную боль ("тяжёлая голова"), шум в ушах, снижение слуха и головокружение. Диагноз ставится на основании патологических изменений ликвора - от значительных до минимальных. Показателями минимальной патологии считаются белок - 0,4%, цитоз - 8 клеток в 1 мм3, глобулиновые реакции (Нонне-Апельта: ++), реакция Ланге больше двух двоек и положительная реакция Вассермана.</p>

<p><span class="bold">Острый генерализованный сифилитический менингит</span> встречается довольно редко. В процесс вовлекаются все оболочки мозга. Заболевание сопровождается усиливающейся головной болью, головокружением, шумом в ушах, рвотой (рвота не связана с приёмом пищи, "мозговая рвота"). Объективно отмечаются ригидность затылка, положительный симптом Кернига, положительный нижний симптом Брудзинского. Регистрируются патологические рефлексы Бабинского, Оппенгейма, Россолимо. Ликвор резко изменён: белок до 1,2%, цитоз от 200 до 1000 клеток в 1 мм3, реакции Нонне-Апельта и Панди ++++, реакция Ланге в виде паралитической или менингитической кривой.</p>

<p><span class="bold">Менингоневритическая форма сифилитического менингита</span> (базальный менингит) характеризуется поражением отдельных участков оболочек головного мозга, чаще в области основания мозга. Клиника этого поражения слагается из симптомов менингита и неврита. Кроме головной боли и головокружения, изредка возникают тошнота и рвота. Симптомы Кернига, Брудзинского, патологические рефлексы отсутствуют. У больных отмечается лёгкий птоз, ассиметрия лица, сглаженность носогубной складки, отклонение языка, опущение мягкого нёба. В ликворе патология умеренно выражена: белок до 0,6-0,7%, цитоз 20-40 клеток в 1 мм3, реакции Нонне-Апельта и Панди положительные или резко положительные; реакция Ланге имеет вид менингитической кривой; реакция Вассермана в ликворе положительная или резко Положительная.</p>

<p><span class="bold">Сифилитическая гидроцефалия</span> возникает в случаях ограниченного воспалительного поражения мозговых оболочек. Заболевание проявляется нарастающей головной болью, головокружением, тошнотой, неукротимой рвотой, спутанностью сознания. Иногда присоединяются эпилептиформные припадки, нарушения речи. Характерным симптомом гидроцефалии является улучшение общего состояния больного (и головной боли) после люмбальной пункции. В ликворе определяется белково-клеточная диссоциация: много белка (1-2%) при небольшом количестве клеток (10-15 в 1 мм3); реакции Нонне-Апельта и Панди положительные; реакция Вассермана в ликворе положительная.</p>

<p><span class="bold">Ранний менинговаскулярный сифилис</span> характеризуется умеренным вовлечением в процесс мозговых оболочек. Клиническая картина при этом разнообразна: головная боль, поражение черепных нервов, нарушение чувствительности, гемипарезы, альтернирующие параличи, эпилептиформные припадки, нарушение памяти, афазия.</p>

<p><span class="bold">Сифилитический менингомиелит</span> (воспаление мягких мозговых оболочек и специфический эндартериит спинного мозга) возникает у не лечившихся или недостаточно лечившихся больных ранним сифилисом. Заболевание возникает "внезапно", имеет довольно острое течение и приводит к параплегии нижних конечностей с глубоким нарушением трофики, снижением или потерей различных видов чувствительности, нарушением сфинктеров. Диагноз помогают установить положительные серологические реакции крови (РВ, РИТ, РИФ), и патология ликвора (РВ в нём всегда положительная).<br />
<br />
<span class="bold">Выделяют следующие формы позднего нейросифилиса:</span> поздний скрытый сифилитический менингит; поздний диффузный менинговаскулярный сифилис; сифилис сосудов мозга (васкулярный сифилис); сухотка спинного мозга, спинная сухотка, табес; прогрессивный паралич; гумма мозга.</p>

<p><span class="bold">Поздний скрытый (латентный) сифилитический менингит</span> возникает и диагностируется через много лет после инфицирования сифилисом. Больные не предъявляют жалоб или иногда отмечают нерезкую головную боль, шум в ушах, снижение слуха и головокружение. Диагностика базируется на патологических изменениях в ликворе, где воспалительный компонент выражен незначительно. Реакция Вассермана и реакция Ланге, как правило, патологические.</p>

<p><span class="bold">Поздний диффузный менинговаскулярный сифилис</span> характеризуется умеренным вовлечением в процесс мозговых оболочек. Больные жалуются на несильную головную боль, реже - на головокружение. Симптомы болезни часто напоминают гипертонический криз. Могут возникнуть нарушения чувствительности, парестезии, гемипарезы, эпилептиформные припадки, альтернирующие параличи, расстройства речи и памяти. Серологические реакции крови положительны у 50% больных. Возможны патологические изменения ликвора - невысокое содержание белка (до 0,6- 0,7%), незначительный цитоз (до 20-30 лимфоцитов в 1 мм3), слабоположительные глобулиновые реакции, реакция Вассермана отрицательная. Не леченный менинговаскулярный сифилис может трансформироваться в спинную сухотку.</p>

<p><span class="bold">Сифилис сосудов мозга (васкулярный сифилис)</span> характеризуется вовлечением в процесс только сосудов мозга. Ликвор не изменяется в связи с отсутствием явлений менингита и поражения серого вещества мозга. Стандартные серологические реакции крови в 60-70% случаев отрицательны, что затрудняет диагностику. При диагностике следует учитывать относительную доброкачественность в начальном периоде заболевания, периодичность течения, "разбросанность" симптомов, положительные классические серологические реакции крови у 30-40% больных и, как правило, положительные РИТ и РИФ. Имеются неврологические расстройства (моно-, пара-, геми- и диплегии, гемипарезы, параличи, афазии, нарушения чувствительности, патологические рефлексы). Могут возникнуть различные расстройства психической сферы, эпилептические припадки по типу джексоновской эпилепсии или общих судорог.</p>

<p><span class="bold">Сухотка спинного мозга (tabes dorsalis)</span>, спинная сухотка, табес. При спинной сухотке поражение локализуется в задних корешках, задних столбах и оболочках спинного мозга.</p>

<p>Клинические симптомы сухотки спинного мозга:</p>

<ul class="list">
	<li>боли стреляющие, кинжальные, сверлящие и рвущие;</li>
	<li>парестезии - чувство опоясывания, сжатия, сдавления на определенных уровнях туловища; чувство онемения, "ползания мурашек", покалывание в ногах;</li>
	<li>расстройство мочеиспускания, дефекации и появление импотенции;</li>
	<li>парезы черепных нервов и как результат - птоз, косоглазие, девиация языка, ассиметрия лица;</li>
	<li>зрачковые расстройства - анизокория (изменение формы и величины зрачков), миоз (сужение зрачков), мидриаз (расширение зрачков), патогномоничный симптом Аргайлла Робертсона (отсутствие реакции зрачков на свет или очень вялая реакция);</li>
	<li>костно-воздушная диссоциация (костная проводимость снижается, воздушная проводимость не изменяется);</li>
	<li>расстройство координации (атаксия) - появление характерной атаксической походки: больной сначала становится на пятки, затем на всю стопу;</li>
	<li>пошатывание в позе Ромберга; нарушения при пальценосовой и пяточно-коленной пробах;</li>
	<li>расстройства глубокого мышечно-суставного чувства;</li>
	<li>отсутствие коленных и ахилловых рефлексов;</li>
	<li>нарушение трофики, холодовая гиперестезия в области спины, болевая гипестезия в области груди.</li>
</ul>

<p>Стандартные серологические реакции у 25-50% больных спинной сухоткой отрицательные. РИТ положительна более чем в 95% случаев. У 50% больных спинной сухоткой спинномозговая жидкость находится в пределах нормы. При патологических изменениях в ней отмечаются незначительное увеличение количества белка (до 0,45 - 0,55V.), повышение числа лимфоцитов (20-30 в 1 мм3), положительные глобулиновые реакции (+++ или ++++) и реакция Вассермана; реакция Ланге имеет характер паралитической кривой.</p>

<p><span class="bold">Прогрессивный паралич</span> проявляется через 20-40 лет после заражения у недостаточно или вообще не лечившихся по поводу сифилиса. В основе развития прогрессивного паралича лежит поражение вещества большого мозга, чаще в области коры передней доли, возникающее в связи с воспалительными изменениями капилляров мозга. В острый период заболевания отмечаются полный распад личности, деградация, прогрессирующее слабоумие, различные формы бреда, галлюцинации, кахексия.</p>

<p>В фазе законченного развития процесса различают <span class="bold">4 формы прогрессивного паралича:</span> дементная; экспансивная; ажитированная; депрессивная.</p>

<p>При дементной форме больной теряет интерес к окружающему, быстро наступают апатия, отупение и прогрессирующее слабоумие. Экспансивная форма характеризуется эйфорией, бредом величия (мегаломания), психомоторной активностью с постепенным распадом психики. У больных ажитированной формой преобладает возбуждение и склонность к разрушительным поступкам. Для депрессивной формы характерны подавленность, тревога, ипохондрический бред.</p>

<p>Клиническая картина прогрессивного паралича слагается из нарушений психической сферы, различных неврологических симптомов и данных лабораторного обследования.</p>

<p>Крайним психическим симптомам относятся следующие: - изменение личности - вспыльчивость, прожорливость, эйфория или вялость, плаксивость, необоснованное благодушие;</p>

<ul class="list">
	<li>нарушение памяти - её снижение на ближайшие события при сохранении на отдельные;</li>
	<li>нарушение счета - связано с нарушением психики, страдает логика счета; - нарушение письма - больной пропускает буквы, слоги, слова; - нарушение речи - быстрая скороговорка или, наоборот, замедленная речь; дизартрия - "спотыкание" на слогах.</li>
</ul>

<p>К неврологическим симптомам относятся зрачковые расстройства, симптом Аргайлла Робертсона (в 12-20% случаев), нарушения чувствительности и двигательной сферы, эпилептиформные припадки, анизорефлексия (разница в рефлексах на обеих половинах тела).</p>

<p>Заболевание подтверждается положительными серологическими реакциями (в 95-97% случаев), положительными РИТ и РИФ (в 90-94% случаев) и изменением спинномозговой жидкости (в 100% случаев) - реакция Ланге даёт характерную паралитическую кривую.</p>

<p>При наличии у больного симптомов прогрессивного паралича и спинной сухотки ставится диагноз табопаралича.</p>

<p><span class="bold">Гумма головного или спинного мозга</span> встречается редко. Обычно она локализуется на выпуклой поверхности или в области основания полушарий большого мозга. Неврологическая картина характеризуется очаговой симптоматикой в сочетании с головной болью и повышением внутричерепного давления. При гумме спинного мозга могут развиться симптомы полного его поперечного поражения. В спинномозговой жидкости отмечаются белково-клеточная диссоциация (повышенное содержание белка при низком уровне лимфоцитов), положительные реакции Вассермана, РИТ и РИФ. Кроме того, РИТ и РИФ положительны в сыворотке крови.<br />
<br />
<span class="bold">Профилактикой поздних форм нейросифилиса</span> является полноценное лечение ранних форм сифилиса, а также исследование спинномозговой жидкости при снятии больных с учета. Спинномозговую жидкость необходимо исследовать у всех больных с клинически выраженными симптомами нейросифилиса с целью диагностики и для определения качества лечения.</p>

	        	
		
