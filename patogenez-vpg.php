<p>Латентность и нейровирулентность ВПГ-инфекции - это основные свойства, определяющие патогенез ВПГ-инфекции. Разнообразные проявления латентности, реактивации и вирулентности у различных пациентов возможно, определяются как свойствами самого вируса, так и характеристиками хозяина. Латентность - это свойство вируса персистировать в организме инфицированного хозяина в отсутствие вирусной репликации и патогенности. Это позволяет вирусу избегать воздействия иммунного ответа хозяина. Реактивация может приводить к клинически бессимптомному выделению вируса, возникновению поражений кожи и слизистых, или появлению редких неврологических симптомов. Среди вирусных факторов, принимающих участие в реактивации, существенную роль играет тимидинкиназа, поскольку этот фермент необходим для репликации вирусной ДНК в постмитотических нейронах.</p>
