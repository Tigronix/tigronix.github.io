<p>
<span class="bold">Нейроинвазия</span> - это способность вируса приобретать доступ из периферических нервных клеток в ЦНС, а нейровирулентность - способность вируса реплицироваться в ЦНС и вызывать неврологические заболевания.<br />
<br />
 Герпес оказывает разрушительное действие на иммунную систему больного и является причиной вторичного иммунодефицита. Рецидивирующий генитальный герпес, нарушая нормальную половую жизнь пациентов, нередко вызывает нервно-психические расстройства. Тропизм ВПГ к эпителиальным и нервным клеткам объясняет полиморфизм клинических проявлений герпетической инфекции. Поражение кожи и слизистых оболочек - самое распространённое клиническое проявление болезни, а поражение ВПГ гениталий относится к наиболее частым заболеваниям, передаваемым половым путём.<br />
<br />
 Процент инфекций, вызванных двумя типами вируса, варьирует от 1 до 86, возможно, отражая разницу в популярности орального секса в различных группах населения. Имеются сведения о том, что с ростом осведомлённости о якобы безопасном оральном сексе в определённых популяциях люди применяют его без презервативов и, как следствие этого, возрастает процент генитальных инфекций, вызываемых ВПГ-2.<br />
<br />
 Заболевание передаётся преимущественно при сексуальных контактах от больного генитальным герпесом или носителем ВПГ. Контакт может быть генито-генитальным, орально-генитальным, генитально-анальным или орально-анальным. Заражение может происходить в том случае, когда у партнёра, являющегося источником инфекции, рецидив болезни или, что особенно важно, когда он выделяет вирус, не имея при этом клинических симптомов. Нередко генитальный герпес передаётся от лиц, не имеющих симптомов заболевания на момент полового акта или даже не знающих о том, что они инфицированы. He исключена возможность инфицирования гениталий при орально-генитальном контакте при наличии у партнёра герпеса лица, а также бытовым путём через средства личной гигиены.<br />
<br />
 Риск неонатального инфицирования новорожденного зависит от формы генитального герпеса у матери и составляет от 0,01% до 75%. Внутриутробная инфекция, вызванная ВПГ-2, встречается крайне редко - примерно в 5% случаев. В подавляющем большинстве случаев инфицирование новорожденного происходит во время родов при прохождении родового канала. Причём, передача инфекции возможна как при наличии очагов поражения в области шейки матки и вульвы, так и при бессимптомном выделении вируса.<br />
<br />
 Вирусемия во время беременности может явиться причиной гибели плода, вызывая до 30% спонтанных абортов на ранних сроках беременности и свыше 50% поздних выкидышей. ВПГ занимает второе место после вируса краснухи по тератогенности. Передача инфекции и развитие неонатального герпеса зависит от ряда обстоятельств:</p>

<ul class="list">
	<li>уровня материнских нейтрализующих антител, перешедших к плоду трансплацентарно, или местных антител, связывающих вирус непосредственно в генитальном тракте;</li>
	<li>длительного безводного промежутка (4-6 часов);</li>
	<li>применения различных инструментов при родоразрешении, приводящего к повреждению кожи ребенка (наложение электродов на голову).</li>
</ul>

