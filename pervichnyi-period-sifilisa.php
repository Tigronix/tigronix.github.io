<p>Признаки эрозивного (язвенного) твёрдого шанкра: ярко-красный (медный) цвет "свежего мяса"; дно эрозии блестящее, лакированное; дно язвы грязно-жёлтого цвета; края эрозии чётко отграничены; язва блюдцеобразная с покатыми краями; округлая или овальная форма; отсутствие признаков острого воспаления; безболезненность; наличие инфильтрата у основания эрозии или язвы.</p>

<p>В большинстве случаев <span class="bold">твёрдый шанкр (первичная сифилома)</span> находится в области наружных половых органов, однако может располагаться на любом участке кожного покрова или видимых слизистых оболочек. Для первичного периода сифилиса возможна экстрагенитальная локализация очага поражения (слизистая полости рта, область заднего прохода). Описаны шанкры на пальцах рук, на конъюнктиве век, на кончике носа, ушных раковинах, волосистой части головы, на коже затылка, спицы, голеней, на подошве.</p>

