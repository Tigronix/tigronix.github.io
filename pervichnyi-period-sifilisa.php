<p>Несмотря на характерные клинические признаки, необычная локализация и вид первичной сифиломы затрудняют её распознавание. К таким атипичным шанкрам относят: шанкрамигдалит; шанкрпанариций; индуративный отёк.</p>

<p><span class="bold">Шанкрамигдалит</span> - специфический амигдалит, характеризующийся увеличением и уплотнением нёбной миндалины без образования эрозии или язвы, но сопровождающийся болезненностью при глотании. Одностороннее поражение, отсутствие лихорадки и других нарушений общего состояния, одностороннее увеличение подчелюстных и шейных лимфатических узлов помогают поставить правильный диагноз.</p>

<p><span class="bold">Шанкрпанариций</span> локализуется обычно на дистальной фаланге указательного пальца, который представляется отёчным, синюшно-красным, булавовидным. На тыльной стороне этого пальца располагается язва с неровными изъеденными краями и дном с некротически-гнойным распадом. У больных имеются резкие, стреляющие боли. Локтевые и подмышечные лимфатические узлы увеличены и болезненны.</p>

<p><span class="bold">Индуративный отёк</span> развивается вследствие поражения лимфатических капилляров кожи на половом члене и коже мошонки у мужчин, а также на малых и больших половых губах у женщин. Поражённый орган значительно увеличивается в размерах, кожа становится плотной, слегка синюшной. При надавливании ямки не остаётся, пальпация безболезненна.<br />
<br />
Через 5 - 8 дней после возникновения твёрдого шанкра начинают увеличиваться близлежащие лимфатические узлы (регионарный сифилитический склераденит), может развиваться специфический лимфангиит. <span class="bold">Регионарный лимфаденит</span> является вторым важным симптомом первичного периода сифилиса. При локализации его в области половых органов увеличиваются паховые лимфатические узлы, на нижней губе или подбородке - подчелюстные, на языке - подбородочные, на верхней губе и веках -предушные, на пальцах рук - локтевые и подмышечные, на нижних конечностях -подколенные и бедренные, на шейке матки - тазовые, в области молочных желез - подмышечные. <span class="bold">Регионарный склераденит</span> проявляется увеличением лимфатических узлов иногда до размеров лесного ореха.</p>

<p><br />
 Признаки регионарного склераденита: увеличенные лимфатические узлы, безболезненность, отсутствие изменений цвета кожи, плотноэластическая консистенция, подвижные узлы, узлы не спаяны между собой и с подлежащими тканями.<br />
<br />
Третьим симптомом первичного сифилиса являются положительные серологические реакции. Реакция Вассермана становится положительной через 3-4 недели после появления твёрдого шанкра, и с этого момента первичный серонегативный сифилис переходит в стадию первичного серопозитивного сифилиса.<br />
 В результате присоединения вторичной инфекции и несоблюдения правил личной гигиены у некоторых больных развиваются осложнения твёрдого шанкра, чаще островоспалительного характера:</p>

<ul class="list">
	<li>баланит - воспаление эпителия головки полового члена;</li>
	<li>баланопостит - баланит в сочетании с воспалением внутреннего листка крайней плоти;</li>
	<li>фимоз - отёк и увеличение в объёме крайней плоти, сужение кольца крайней плоти;</li>
	<li>парафимоз - ущемление головки полового члена и отёк крайней плоти, "удавка";</li>
	<li>гангренизация - язвенно-некротический процесс захватывает всю поверхность твёрдого шанкра с образованием серо-бурого плотного струпа, по отторжении которого обнажается язвенный дефект с гнойно-геморрагическим отделяемым;</li>
	<li>фагеденизм - процесс прогрессирует по периферии и вглубь с обширным, глубоким разрушением окружающих и подлежащих тканей крайней плоти, головки полового члена, уретры.</li>
</ul>

<p><br />
<span class="bold">Диагноз первичного периода сифилиса</span> устанавливается на основании следующих данных:</p>

<ul class="list">
	<li>анамнез заболевания, конфронтация с предполагаемым источником заражения;</li>
	<li>клинические признаки: наличие безболезненной эрозии или язвы со скудным отделяемым и уплотнённым основанием, наличие регионарного склераденита;</li>
	<li>в серонегативной стадии - обнаружение трепонем в отделяемом из очагов поражений;</li>
	<li>в серопозитивной стадии - положительные серологические реакции.</li>
</ul>

<p>Диагноз первичного сифилиса, поставленный только на основании типичной клинической картины, не подтверждённый обнаружением бледной трепонемы или положительными серологическими реакциями, не может считаться правомочным.<br />
<br />
При дифференциальной диагностике твёрдого шанкра необходимо отличать его от эрозий и язв при других заболеваниях, локализующихся прежде всего в области половых органов.</p>

<p><span class="bold">Эрозия</span> травматического происхождения (при активных половых актах, травмировании волосами, при введении в кожу инородных тел) имеет линейную форму с мягким основанием, сопровождается островоспалительными явлениями, болезненна, быстро заживает. Бледные трепонемы не обнаруживаются в отделяемом эрозии.</p>

<p><span class="bold">Мягкий шанкр (шакроид)</span> имеет более короткий, чем сифилис инкубационный период (1-5 дней), характеризуется появлением множественных воспалительных элементов на половых органах и в перигенитальной области: вначале пятна, затем - папулы, везикулы, пустулы. Пустулы изъязвляются. Края язв ярко-красного цвета, отёчные, подрытые, с гнойным отделением, болезненны. Вокруг язв отмечается островоспалительный венчик, инфильтрат в основании мягкий. В отделяемых язвах обнаруживают возбудителя мягкого шанкра: стрептобациллы Дюкрея-Петерсена-Унны. Имеется островоспалительная лимфаденопатия: резкая болезненность, мягкая консистенция, периаденит, покраснение кожи, очаги флюктуации, образование свищей, гнойное отделяемое.</p>

<p><span class="bold">Трихомонадные и гонококковые язвы</span> характеризуются воспалительными явлениями, ярко-красного цвета, с обильным отделяемым, в котором обнаруживают соответствующие возбудители. Сопутствующего регионарного склераденита не имеется.</p>

<p><span class="bold">Простой пузырьковый лишай</span> представлен мелкими сгруппированными везикулами с серозным содержимым на отёчном основании и гиперемированной коже. Высыпаниям предшествуют зуд, жжение, покалывание в участках поражения. Эрозии простого герпеса множественные, с полициклическими очертаниями без уплотнения у основания, исчезают бесследно.</p>

<p><span class="bold">Стрептококковая эктима</span> характеризуется островоспалительной болезненной язвой с мягким основанием, дно её покрыто грязно-жёлтым гнойным налётом, отсутствует сопутствующий склераденит.</p>

<p><span class="bold">Чесоточная эктима</span> и чесоточные папулы обычно множественные, островоспалительного характера, сопровождаются зудом и наличием других типичных симптомов чесотки, подтверждаются лабораторным обнаружением чесоточного клеща.</p>

<p><span class="bold">Шанкриформная пиодермия</span> характеризуется серозно-геморрагической корочкой (покрывающей эрозию или язву) с плотным основанием, безболезненностью, сопутствующим склераденитом. Очаг поражения правильных округлых или овальных очертаний, края слегка возвышены, дно ровное, чистое. В отделениях язвы и пунктате лимфатических узлов бледные трепонемы не обнаруживаются. Серологические реакции на сифилис отрицательные.</p>

<p><span class="bold">Туберкулёзная язва</span> мягкой консистенции, неправильной формы, края мягкие, подрытые, кровоточит. На дне язвы имеются желтоватого цвета мелкие очаги распада - зёрна Треля. Язва длительно не заживает, не рубцуется. Имеются другие очаги туберкулёзной инфекции.</p>

<p><span class="bold">Острая язва Чаплина-Липшютца</span> на наружных половых органах встречается у молодых девушек. Заболевание протекает остро, с высокой температурой тела. На малых и больших половых губах или в прианальной области возникают резко болезненные язвы округлых или неправильных очертаний, мягкой консистенции, на эритематозном фоне. Диагностика основывается на характерных клинических, бактериологических и серологических данных.</p>

<p><span class="bold">Эритроплазия Кейра</span> проявляется небольшим безболезненным очагом на головке полового члена, чётко отграниченным, ярко-красного цвета с бархатистой влажной поверхностью без отделяемого.</p>

<p><span class="bold">Базальноклеточная карцинома кожи</span> поражает людей пожилого возраста, медленно прогрессирует, не склонна к метастазам. Край язвы образован мелкими белесоватыми узелками. Характерны деревянистая плотность у основания, неровные, несколько размытые, плотные края и валик по периферии.</p>

<p><span class="bold">Плоскоклеточный рак кожи</span> встречается чаще у пожилых людей. Края язвы выворочены, дно изрыто, покрыто очагами распада, легко кровоточит, длительно не заживает. Характерен экзо- и эндофитный рост язвы. Лимфатические узлы увеличены, спаяны друг с другом и окружающими тканями.</p>

	        	
		
