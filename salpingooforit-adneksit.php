	        		<h1>Аднексит</h1>
					
					<p>САЛЬПИНГООФОРИТ (АДНЕКСИТ) – это воспаление яичников и маточных труб. Сальпингоофорит могут вызвать стафилококки, стрептококки, микоплазмы, хламидии, гонококки. Воспалительный процесс начинается в маточных тубах, куда инфекция попадает из полости матки. Далее из маточных труб инфекция переходит  на яичники. Воспалительные  заболевания  труб и яичников  протекают одновременно, поэтому их этот процесс называется сальпингоофорит.Острый сальпингоофорит проявляется повышением температуры, тянущими и колющими болями внизу живота, боли при половом акте,  нарушением мочеиспускания, расстройством кишечника. Возможны гнойные образования в маточных трубах, а так же развитие перитонита. Это очень опасно  и может привести к летальному исходу</p>


