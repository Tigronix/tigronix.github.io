<p>При длительном существовании сальпингоофорита и неправильно подобранном  лечении,  процесс переходит в хроническую форму. Хронические сальпингоофориты проявляются умеренными болями тянущего характера в нижних отделах живота, нарушениями менструального цикла. Обострения могут быть связаны с переохлаждением, фазой менструального цикла, с нервными стрессами, с общими простудными заболеваниями и т.д. Эти осложнения нередко могут протекать малосимптомно и выявляться только при обследовании гинекологом в связи с бесплодием и т. д. Сальпингит может вызвать стойкое бесплодие вследствие облитерации маточных труб и развития спаек вокруг придатков.</p>

