<p>
<span class="bold">2) Вторичный сифилис (syphilis II secundaria)</span> - стадия заболевания, обусловленная гематогенным распространением возбудителей из первичного очага, проявляющаяся полиморфными высыпаниями (розеолы, папулы, пустулы) на коже и слизистых оболочках.<br>
<span class="bold">Вторичный свежий сифилис (syphilis II recens)</span> - период сифилиса, характеризующийся многочисленными полиморфными высыпаниями на коже и слизистых оболочках, полиаденитом, нередко наблюдаются остаточные признаки твёрдого шанкра.<br>
<span class="bold">Вторичный рецидивный сифилис (syphilis II recidiva)</span> - период вторичного сифилиса, который проявляется немногочисленными полиморфными сгруппированными высыпаниями и, иногда, поражением нервной системы.<br>
<span class="bold">Вторичный скрытый сифилис (syphilis II latens)</span> - вторичный период заболевания, протекающий латентно.<br>
<br>
<span class="bold">3) Третичный сифилис (syphilis III tertiaria)</span> - следующая за вторичным сифилисом стадия, которая характеризуется деструктивными поражениями внутренних органов и нервной системы с возникновением в них гумм.<br>
<span class="bold">Активный третичный сифилис (syphilis HI activa)</span> - проявляется активным процессом образования бугорков, разрешающихся некротическим распадом, образованием язв, их заживлением, рубцеванием и появлением неравномерной пигментации (мозаичности).<br>
<span class="bold">Скрытый третичный сифилис (syphilis III latens)</span> - период болезни у лиц, перенесших активные проявления третичного сифилиса.<br>
<br>
<span class="bold">4) Скрытый сифилис (syphilis latens)</span> - сифилис, при котором серологические реакции положительные, но отсутствуют признаки поражения кожи, слизистых оболочек и внутренних органов.<br>
<span class="bold">Ранний скрытый сифилис (syphilis latens praecox)</span> - скрытый сифилис, с момента заражения которым прошло менее 2 лет.<br>
<span class="bold">Поздний скрытый сифилис (syphilis latens tarda)</span> - с момента заражения прошло более 2 лет.<br>
<span class="bold">Неуточнённый скрытый сифилис (syphilis ignorata)</span> - болезнь с неустановленной давностью.<br>
<br>
<span class="bold">5) Врожденный сифилис (syphilis congenita)</span> - сифилис, при котором заражение произошло от больной матери в период внутриутробного развития.<br>
<span class="bold">Ранний врождённый сифилис (syphilis congenita praecox)</span> - врождённый сифилис у плода и у детей младше 2 лет, проявляющийся сифилитической пузырчаткой, диффузной папулёзной инфильтрацией кожи, поражением слизистых оболочек, внутренних органов, костной ткани, глаз.<br>
<span class="bold">Поздний врождённый сифилис (syphilis congenita tarda)</span> - врождённый сифилис у детей старше 2 лет, проявляющийся триадой Гетчинсона, а также поражением кожи, внутренних органов и костей по типу третичного сифилиса.<br>
<span class="bold">Скрытый врождённый сифилис (syphilis congenita latens)</span> - врождённый сифилис, при котором клинические проявления отсутствуют и лабораторные показатели спинномозговой жидкости в норме.<br>
<br>
<span class="bold">6) Сифилис нервной системы (neurosyphilis):</span><span class="bold"> ранний (neurosyphilis praecox)</span> - давность болезни до 5 лет, <span class="bold">поздний (neurosyphilis tarda)</span> - более 5 лет.<br>
<br>
<span class="bold">7) Сифилис висцеральный (syphilis visceral is)</span> - сифилис, при котором поражены внутренние органы (сердце, головной мозг, спинной мозг, лёгкие, печень, желудок, почки).<br>
<br>
<span class="bold">8) Сифилис бытовой </span> - сифилис, заражение которым происходит бытовым путём.<br>
<br>
<span class="bold">9) Сифилис обезглавленный (syphilis decapitata)</span> - заражение происходит при попадании возбудителя непосредственно в кровяное русло (через рану, при исследовании крови); характеризуется отсутствием твёрдого шанкра.<br>
<br>
<span class="bold">10) Сифилис трансфузионный (syphilis transfusionalis)</span> - заражение в результате переливания крови больного.</p>

		
