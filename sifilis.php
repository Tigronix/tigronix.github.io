	<span class="bold">Различают следующие формы сифилиса:</span>

<p><span class="bold">1) Первичный сифилис (syphilis I primaria)</span> - стадия заболевания, характеризующаяся появлением твёрдого шанкра и увеличением регионарных лимфатических узлов.<br>
<span class="bold">Первичный серонегативный сифилис (syphilis I seronegative)</span> - сифилис со стойко отрицательными серологическими реакциями в течение курса лечения.<br>
<span class="bold">Первичный серопозитивный (syphilis I seropositive)</span> - сифилис с положительными серологическими реакциями.<br>
<span class="bold">Первичный скрытый период (syphilis I latens)</span> - сифилис, характеризующийся отсутствием клинических проявлений у пациентов, начавших лечение в первичный период болезни и получивших неполноценную терапию.<br>
</p>
					
