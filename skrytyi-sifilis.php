<p>Различают <span class="bold">ранний (Syphilis latens praecox)</span> и <span class="bold">поздний (Syphilis latens tarda) скрытый сифилис</span>.</p>

<p>1. Ранний скрытый сифилис - приобретенный сифилис с давностью инфекции до 2 лет, без клинических проявлений, с положительными серологическими реакциями.</p>

<p>2. Поздний скрытый сифилис - приобретенный сифилис с давностью инфекции свыше 2 лет, без клинических проявлений, с положительными серологическими реакциями в крови и нормальной спинномозговой жидкостью.</p>

<p>Когда невозможно отличить ранний и поздний сифилис, ставят диагноз неуточнённого сифилиса (lues latens ignorata), который следует рассматривать как предварительный диагноз, подлежащий уточнению в процессе лечения и наблюдения.<br />
<br />
<span class="bold">Диагностические признаки раннего скрытого сифилиса:</span></p>

<p>1. В анамнезе имеются указания на наличие в течение последних двух лет эрозивно-язвенных высыпаний на гениталиях, сыпи на коже туловища, выпадения волос, приём антибиотиков по поводу гонореи и других заболеваний.</p>

<p>2. Данные конфронтации - обнаружение у полового партнера активного или раннего скрытого сифилиса.</p>

<p>3. Данные клинического осмотра - обнаружение рубца или атрофии кожи, уплотнения на месте бывшей первичной сифиломы, увеличение паховых лимфатических узлов.</p>

<p>4. Результаты серологического исследования - положительные стандартные серологические реакции в высоком титре (1:120, 1:160, 1:320) у 2/3 больных, положительная РИФ у всех больных, положительная РИТ у 1/3 больных.</p>

<p>5. Реакция обострения (Герксгеймера Яриша Лукашевича) на введение пенициллина может наблюдаться у 30% больных.</p>

<p>6. Относительно быстрая динамика негативации стандартных серологических реакций в процессе лечения.</p>

<p>7. Ликвор в большинстве случаев нормальный. При патологическом ликворе преобладает воспалительный компонент (белок, цитоз, глобулиновые реакции). 8. Возраст больных - моложе 40 лет.<br />
<br />
<span class="bold">Диагностические признаки позднего скрытого сифилиса:</span></p>

<p>1. Отсутствие анамнестических данных. Иногда имеются сообщения о вероятном заражении 2-3 года назад.</p>

<p>2. При конфронтации у половых партнёров обнаруживают поздний - сифилис, или у них отсутствуют проявления сифилитической инфекции.</p>

<p>3. При клиническом осмотре на коже и видимых слизистых оболочках отсутствуют следы разрешившихся сифилидов.</p>

<p>4. Стандартные серологические реакции положительные в низком титре (1:5, 1:10, 1:20, 1:50) у 90% больных. РИФ и РИТ положительные у всех больных.</p>

<p>5. Реакция обострения на введение пенициллина не наблюдается.</p>

<p>6. Динамика негативации стандартных серологических реакций в процессе лечения очень медленная.</p>

<p>7. Патология ликвора встречается редко. В ликворе преобладает дегенеративный компонент (положительная реакция Вассермана, патологическая реакция Ланге).</p>

<p>8. Возраст больных - преимущественно старше 40 лет.</p>

	        	
		
