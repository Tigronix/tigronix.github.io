<p>Трансвагинальное узи осуществляется при помощи специального датчика, который вводится во влагалище. Такая гинекологическая манипуляция показывает более четкую картинку и не требует подготовки в отличие от других методов исследования, которые использует гинекология.</p>

<p><span class="bold">Гинекология: Использование ультразвукового исследования (узи) в период беременности.</span></p>

<p><span class="bold">Гинекологическое узи</span> в акушерстве очень важно, так как оно помогает убедится, что беременность протекает хорошо, используется такая диагностика, не менее трех раз на разных сроках беременности.</p>

<p>Первое гинекологическое узи проводится на сроке 10-14 недель для того, чтоб определить возможную угрозу выкидыша, внематочную беременность, синдром Дауна и другие возможные патологии. На раннем сроке только узи гинекологическое может определить многоплодную беременность.</p>

<p>Второе узи проводится на сроке 5-6 месяцев, чтоб оценить внутриутробное развитие плода и состояние плаценты, а также благодаря узи, если положение ребенка позволит, можно озвучить и половую принадлежность ребенка.</p>

<p>Третье узи осуществляется перед родами на 30-36 неделях. Доктор определяет размер и уровень развития плода, какое у ребенка предлежание, а так же степень зрелости плаценты. Если с помощью  узи замечены отклонения от нормы, то тогда проводится внутриутробное лечение</p>

<p><span class="bold">УЗИ – это возможность предупредить многие болезни.</span></p>

<p>Очень важно гинекологическое узи и девушки-подростка, что позволит позаботится о здоровье будущей мамы, которое поможет заранее предупредить заболевания.</p>

<p>Каждая женщина просто обязана проходить ультразвуковое обследование не реже чем 1-2 раза в год, не зависимо беспокоит ее что то или нет, так как многие заболевания половой системы протекают бессимптомно и безболезненно. Поэтому чем раньше вы посетите доктора, тем быстрее пройдет лечение, без каких-либо осложнений. Запущенные заболевания могут привести к бесплодию.</p>

