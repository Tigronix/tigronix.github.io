<p>Современные возможности  позволяют выявить и классифицировать большинство известных болезней. Прежде всего, это болезни, связанные с органическими изменениями в связи с острыми и хроническими воспалениями, образованием злокачественных и доброкачественных опухолей, травматическими и послетравматическими изменениями, поражениями сосудов, врожденными аномалиями и дистрофическими изменениями.</p>

<p><img class="img_center" style="width: 550px; height: 300px; margin: 10px;" src="../img/UZI_organov_bryushnoy_polosti_1.jpg.png" alt="узи органов брюшной полости" /></p>

<div class="clr"></div>
<p>Также УЗИ брюшной полости незаменимо, если нужно получить наиболее полную оценку состояния печени, сердца, селезенки, поджелудочной железы и желчевыводящей системы.</p>

<p>Если невозможно точно и конкретно классифицировать заболевание или оценить здоровье пациента (при непонятной истории заболевания, запутанных жалобах пациента противоречивых результатах анализов или недостатке симптомов), то здесь тоже выручит комплексное ультразвуковое обследование.</p>

<p><span class="bold">УЗИ используется и при необходимости высокоточного исследования кровотока (внутренние структуры и брюшную полость):</span></p>

<ul class="list">
	<li>Печени при опухолях, циррозах, гепатитах, тепатозах</li>
	<li>Поджелудочной железы при сахарном диабете, кистах, подозрениях на образование раковых опухолей, панкреатитах;</li>
	<li>Желчного пузыря при появлении камней, образовании подозрительных опухолей, при врожденных аномалиях, образованиях полипов, холециститах;</li>
	<li>Селезенки при гематологических болезнях.</li>
</ul>

<p><span class="bold">Узи органов брюшной полости</span> проводится с целью выявить и предотвратить развитие опухолевого поражения, назначив своевременное лечение или операцию по их устранению. Проводится оно при малейшем подозрении на образование злокачественных опухолей. Используется с целью оценки эффективности хирургического вмешательства и для контроля постоперационного и реабилитационного периода.</p>

<p><img style="width: 350px; height: 281px; margin: 10px; float: left;" src="../img/kisty-pech.jpg" alt="узи кисты печени" /><img style="width: 350px; height: 281px; margin: 10px; float: left;" src="../img/LiverUltrasound.png" alt="узи печени" /></p>

<p><span class="bold">Исследования проводятся на основе нескольких методик ультразвукового сканирования:</span></p>

<ul class="list">
	<li>Высокоразрешающее серошкальное сканирование (черно-белое) – позволяет получить изображение срезов, дает возможность оценить структуру, положение по отношению к другим структурам, сосудистое русло и другие важные показатели с учетом особенностей каждого отдельного пациента;</li>
	<li>Допплеровское черно-белое исследование – позволяет оценить систему кровоснабжения и проследить кровоток в сосудистом русле;</li>
	<li>Допплеровское цветное исследование – то же, что и черно-белое, только позволяет увидеть больше деталей в системе кровотока и сделать более точную оценку;</li>
	<li>Трехмерное ультразвуковое сканирование – самый прогрессивный метод, позволяющий оценить проблему в более полной мере, исследовать его сосудистую сеть и некоторые изменения, возникающие впоследствии болезни, которые невозможно выявить при обследовании другими методами.</li>
</ul>

<p>Большинство врачей, как отечественных, так и зарубежных, утверждает, узи   брюшной   полости помогает не только установить более точный диагноз и выявить детали заболевания, но и значительно сокращает время, необходимое для такого же обследования другими методами.</p>

<p><span class="bold">Ультразвуковое исследование брюшной полости в Москве:</span></p>

<ul class="list">
	<li>Всестороннее исследование, оценка состояния лимфатических узлов и забрюшинного пространства при использовании допплеровских методик узи;</li>
	<li>Оценка состояния печени и состояния ее кровотока с применением допплерографических методик, а также оценка сосудистой сети печени при использовании технологий 3D и 4D исследований.</li>
	<li>Исследование состояния поджелудочной железы с применением технологий 3D и 4D сканирования;</li>
	<li>Оценка состояния селезенки и ее лимфооттока с применением технологий допплеровского исследования и технологий 3D и 4D;</li>
	<li>Оценка при использовании допплеровских методик почек, желчевыводящей системы, мочевого пузыря и системы их кровоснабжения;</li>
	<li>При необходимости трансабдоминиальное исследование предстательной железы при использовании узи брюшной полости.</li>
</ul>

	        	
		
