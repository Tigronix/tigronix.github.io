<p>При УЗИ малого таза у женщины проверяются матка, яичники, мочевой пузырь, влагалище и маточные трубы. УЗИ может проводиться двумя способами: вагинальным (влагалищным) и при помощи абдоминиальных датчиков (через живот).</p>

<p><img style="width: 350px; height: 250px; margin: 10px; float: left;" src="../img/nils-bil.jpg" alt="УЗИ органов малого таза" /><img style="width: 350px; height: 250px; margin: 10px; float: left;" src="../img/c49b6e8e8dc94aba8c92b8e556d5b1da.jpg" alt="УЗИ органов малого таза" /></p>

<div class="clr"></div>

<p>При УЗИ матки определяются ее размеры, положение, строение стенок и форма.</p>

<p>Отдельно обследуется эндометрий и полость матки, внутриматочные структуры. Полость матки беременной женщины щелевидная. Ее внутренний слой – эндометрий – меняет свои свойства на протяжении менструального цикла.</p>

<p>Яичники обследуют на наличие новообразований, также определяется их форма, положение, размеры и строение тканей.</p>

<p>Также определяют наличие свободной жидкости (которой при нормальных условиях выхода яйцеклетки из яичника совсем немного) и наличие чужеродных новообразований в области малого таза.</p>

<p>Также обследуется мочевой пузырь на наличие новообразований в виде опухолей и камней, обследование должно проводиться при достаточном наполнении мочевого пузыря.</p>

<p><span class="bold">Преимущества диагностики малого таза при помощи УЗИ</span></p>

<p>УЗИ органов– это один из самых безопасных методов обследования и совершенно безвреден даже для беременных женщин. Кроме того УЗИ не требует больших затрат времени и усилий для диагностики, при этом очень нагляден и может неоднократно проводиться в течении короткого промежутка времени.</p>

<p><span class="bold">Показания к УЗИ органов малого таза</span></p>

<p>УЗИ применяется для выявления гинекологических заболеваний, для определения беременности и контроля развития плода, для определения успешности лечения. Современные технологии УЗИ позволяют определить беременность при обследовании матки на самых ранних стадиях.<br />
УЗИ органов малого таза необходимо проводить при различных нарушениях менструального цикла у женщин: задержка менструации, ее преждевременное появление, сильном кровотечении во время менструации или его отсутствии, при выделениях из влагалища или болях внизу живота.</p>

<p>Гинекологическое ультразвуковое исследование позволяет обнаружить различные виды заболеваний: образование опухолей в матке или яичниках, воспалительные процессы и другие заболевания, в числе которых эндометрит, кисты, сальпингоофорит и прочие.</p>

