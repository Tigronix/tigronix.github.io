<p>Своевременное УЗИ органов дает возможность обнаружить миому матки еще на ранних стадиях.</p>

<p>Также УЗИ малого таза широко применяется при контроле процесса лечения бесплодия  и при планировании беременности, для мониторинга яичного фолликулярного аппарата.</p>

<p>Назначается УЗИ при лечении гормональными средствами, при приеме противозачаточных препаратов и при наличии внутриматочного конрацептива для контроля и предотвращения возможных осложнений и проблем.</p>

<p>При беременности УЗИ позволяет следить за развитием плода и своевременно определять его возможные патологии.</p>

<p>В урологии узи малого таза применяется при проблемах с мочеиспусканием, для определения состояния мочеиспускательной системы.</p>

<p><span class="bold">Противопоказания</span></p>

<p>Противопоказаний для проведения УЗИ органов малого таза в принципе не существует.</p>

<p><span class="bold">Подготовка к процедуре УЗИ для обследования</span></p>

<p>Для посещения кабинета ультразвуковой диагностики с собой нужно иметь полотенце или салфетки для удаления остатков геля после окончания процедуры, а также пеленку или покрывало, на которое можно лечь для проведения процедуры.</p>

<p>Обычное гинекологическое УЗИ у беременных женщин проводится с полным мочевым пузырем, кроме отдельных случаев, оговоренных с врачом.</p>

<p><span class="bold">Для наиболее точного и эффективного ультразвукового обследования  нужно придерживаться некоторых правил:</span></p>

<ul class="list">
	<li>Перед трансабдоминиальным (через живот) УЗИ нужно выпить за час до обследования 1-1,5 литра негазированной жидкости и не мочится до проведения процедуры УЗИ малого таза;</li>
	<li>При трансвагинальном исследовании ограничений не существует, можно проводить процедуру при порожнем мочевом пузыре;</li>
	<li>При акушерском (во время беременности) УЗИ нужно на время процедуры иметь умеренно заполненный мочевой пузырь. Для этого нужно за час до проведения УЗИ выпить 1-2 стакана жидкости;</li>
	<li>Ультразвуковая диагностика мочеполовой системы проводится также при умеренно заполненном мочевом пузыре. Для этого также нужно выпить 0,5 л жидкости за час до обследования или просто два часа не мочится – это позволит заполненному мочевому пузырю оттеснить обследуемые структуры;</li>
	<li>Очень важным условием для успешного проведения ультразвукового исследования является пустой кишечник и отсутствие в нем газов. Нужно 2-3 дня перед обследованием соблюдать особую диету и не употреблять продуктов, способных вызвать запоры и загазованность, прежде всего следует удерживаться от употребления молочных продуктов, черного хлеба, свежих фруктов и овощей. Также можно в этот период принимать ферментные препараты, такие как фестал, креон, энзистал и др. Также можно принимать препараты типа эспумизан, активированный уголь и укропную воду, делать очистительные клизмы не рекомендовано, так как они могут только усилить газообразование. В случае запора следует принять слабительное или прибегнуть к исследованию при помощи ректального датчика.</li>
	<li>Ультразвуковое исследование должно проводиться строго натощак, последний прием пищи должен быть не ранее чем за 8-12 часов до исследования, и сразу же после опорожнения кишечника;</li>
	<li>При ультразвуковой диагностике матки, молочных желез и маточных придатков исследование нужно проводить в первой половине или средине менструального цикла;</li>
	<li>Ультразвуковое исследование на фолликулогенез проводится строго в 5-9, 11-15 дни менструального цикла;</li>
	<li>В некоторых случаях УЗИ может проводиться без подготовки (как правило, экстренных случаях), но его эффективность при этом будет значительно ниже.</li>
</ul>

<p><span class="bold">Как проходит процедура УЗИ органов малого таза</span></p>

<p>Пациент ложится на кушетку (предварительно застелив пеленку или покрывало) головой к врачу и аппарату, оголяет живот и его низ. Врач смажет датчик аппарата УЗИ гелем (или оденет презерватив и смажет его гелем при трансвагинальном исследовании) и начнет водить им по животу, изредка будет надавливать, чтобы увидеть орган под другим углом. Как правило, процедура эта безболезненна, однако могут возникать небольшие боли во время надавливания при наличии острых воспалительных процессов в организме.  В зависимости от цели проведения, процедура занимает 10-20 минут.</p>

