<p><span class="bold">Осложнения после проведения ультразвукового исследования</span></p>

<p>После проведения УЗИ осложнений возникать не должно, однако нужно знать, что трансвагинальное исследование при беременности проводится только после оценки риска для плода.</p>

<p><span class="bold">Расшифровка результатов </span></p>

<p>Эту процедуру может проводить только грамотный квалифицированный врач.</p>

<p>Что может быть обнаружено при проведении исследования:</p>

<ul class="list">
	<li>Врожденные особенности и аномалии: современные технологии дают возможность увидеть врожденные аномалии развития матки – ее неправильная форма, удвоение и т.д. Врожденные аномалии и особенности могут быть причинами бесплодия, способствовать нарушению родовой функции, быть причиной преждевременных родов, неправильного развития плода или внутриутробной гибели плода;</li>
	<li>Эндометриоз: при этом патологическом процессе маточный эндометрий разрастается за пределы ее полости. При проведении исследования можно определить как внутренний эндометриоз – разрастание эндометриоза на стенки матки, и внешний – разрастание его на соседние органы. Эта болезнь может вызывать осложнения в период беременности, в некоторых случаях и вовсе может быть причиной бесплодия;</li>
	<li>Миома матки: это опухоль, возникающая в женской половой системе (как правило, доброкачественная). При проведении УЗИ обследуются миоматозные узлы, их расположение и размеры, определяется наличие миоматозных опухолей и их количество. При наличии опухолей ультразвуковое исследование позволяет следить за динамикой их развития и вовремя назначать лечение или оперативное вмешательство, поэтому подобное исследование проводится раз в несколько месяцев. Важно провести исследование на наличие миомы перед зачатием, так как опухоли могут серьезно повлиять на течение беременности и развитие плода;</li>
	<li>Диагностика беременности: в этом случае проведение УЗИ позволяет определить беременность на ранних стадиях, начиная уже с 3-4 недели. На самых ранних стадиях беременность можно определить только трансвагинальным датчиком аппарата с высококачественным расширением. Также обследовании можно обнаружить и внематочную беременность (трубную, яичниковую, шейную), когда плодное яйцо прикрепляется в непредназначенное для этого место, что может вызвать серьезные заболевания;</li>
	<li>Внутриматочная контрацепция: возможно контролировать при помощи ультразвукового исследования процесс установки или удаления внутриматочного контрацептива, выявлять осложнения, вызванные его неправильным положением или врастанием в стенки матки, его выпадение из полости матки и т.д. Если планируется беременность после удаления контрацептива, то нужно обязательно пройти обследование;</li>
