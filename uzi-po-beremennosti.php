<div class="clr"></div>

<p>Главной целью ультразвукового исследования (узи) в период беременности является определение группы высокого риска по рождению детей с патологиями. Для этого во время проведения узи при беременности, изучается толщина воротникового пространства, изображение костей носа, лицевой угол, кровоток в венозном протоке. При беременности узи на сроке 11-14 недель позволяет снизить вероятность рождения ребенка с хромосомными патологиями, также узи позволяет исключить необходимость проведения инвазивной диагностики. Самой эффективной считается сочетание двухмерного и трехмерного узи.</p>

<p>УЗИ на разных сроках беременности позволяет изучить такие показатели, как: количество плодов в утробе, предлежание плода, структуру плаценты и ее толщину, количество околоплодной жидкости, определяют срок беременности по размерам самого плода и его органов, также по узи определяют пол ребенка после 12 недель.</p>

<p>Доплер УЗИ проводят в 2-3 триместре беременности для оценки характера и скорости кровотока в сосудах, это позволяет своевременно найти нарушения в системе мать-плацента-плод. Самым изучаемым сосудом при беременности является артерия пуповины, так доктора определяют при помощи узи, в полной ли мере плод получает, все питательные вещества и кислород.</p>

<p><img style="width: 250px; height: 173px; margin: 10px; float: left;" src="../img/ccs-12347-0-59446000-1417004119.jpg" alt="узи в первом триместре" /></p>



<p>Самое <span class="bold">первое ультразвуковое исследование узи</span> позволяет уточнить факт зачатия и дает возможность на ранних сроках определить осложнения: угрозу выкидыша, внематочную или замершую беременность и другие отклонения. Также при узи определяется многоплодная беременность.</p>

<div class="clr"></div>



<p><img style="width: 250px; height: 188px; margin: 10px; float: left;" src="../img/3877_1346071878_8.jpg" alt="узи во втором триместре беременности" /></p>

<p><span class="bold">Второе узи</span> делается в период с 11-13 недели, таким образом проводится комплексная оценка эхографических маркеров хромосомных аномалий: лицевой угол, толщина воротникового пространства, кровоток в венозном протоке, изображение костей носа. На таком сроке при помощи узи определяются такие патологии, как: Экзенцефалия – анэнцефалия. Гастросшизис и омфалоцеле. Такой диагноз ставится только после 12 недель беременности, так как до этого срока физиологическая пупочная грыжа не имеет клинического значения.</p>

<p>1 триместр беременности завершает 12 неделя, если ребенок будет правильно лежать, то можно уже определить пол малыша.</p>
<div class="clr"></div>
<p><img style="width: 250px; height: 187px; margin: 10px; float: left;" src="../img/index.jpeg" alt="узи в третьем триместре беременности" /></p>

<p><span class="bold">Третье узи</span> назначается на 20-24 неделе, на этом сроке уточняется срок, предлежание плода, определяется пол малыша, смотрят состояние плаценты, определяется количество и качество околоплодной жидкости, так же смотрят кровоток в артерии пуповины и маточных артериях.</p>

<p>Четвертое узи рекомендуют делать на сроке 32-34 недели. На данном сроке узи позволяет определить предлежание плода, обычно он уже принимает устойчивое положение, помогает акушеру-гинекологу определить способ родов, а так же позволяет определить пороки развития.</p>
<div class="clr"></div>
<p>Узи на других сроках проводятся по назначению врача. На сегодняшний день можно определить сердцебиение сердца уже с 4 недель, а двигательная активность видна уже с 8 недель. При помощи узи определяется расположение и особенности плаценты, поэтому узи рекомендуют проводить при патологических состояниях, таких как предлежание и преждевременная отслойка плаценты, перенашивание или резус-конфликт.</p>

<p>В нашей клинике в Москве на Авиамоторной вы можете сделать узи по беременности по приемлемым ценам и на современных аппаратах, узи безопасно как для матери, так и для ребенка.</p>

	        	
		
