<p><br />
Можно только предполагать о причинах изменений в составе микробной флоры, приводящих к развитию бактериального вагиноза. Бактериальный вагиноз ассоциируется со сменой полового партнера, курением, спринцеванием и чаще обнаруживается у женщин, принадлежащих к афрокарибской этнической группе и у лесбиянок. Эти разрозненные наблюдения с трудом складываются в единую гипотезу, способную объяснить возникновение бактериального вагиноза. Все исследования включали самостоятельный забор женщинами образцов из влагалища с помощью тампонов. Забор осуществляли с определенными интервалами на протяжении менструального цикла или в течение гораздо более длительного периода времени. При этом были выявлены три основных варианта микробиоценоза.<br />
<br />
Первый и наиболее часто встречающийся вариант представлял собой нормальную картину микробиоценоза с преобладанием лактобактерий (I степень чистоты), которая сохранялась на протяжении всего периода исследования.<br />
Второй вариант был представлен нормальной микрофлорой, состав которой периодически резко изменялся с появлением, в основном, ассоциированных с бактериальным вагинозом бактерий, но с сохранением некоторого количества лактобактерий (II степень чистоты), или с полным замещением лактобактерий бактериями, ассоциированными с бактериальным вагинозом (III степень чистоты).<br />
Третий вариант встречался реже и характеризовался стабильно аномальной картиной микробиоценоза. Изменения в составе микрофлоры часто носили временный характер. Наблюдались быстрые переходы от II или III к I степени чистоты. У некоторых женщин состав микрофлоры начинал меняться вскоре после появления признаков вагинального кандидоза. Во многих случаях изменения происходили, в основном, в первой половине менструального цикла, когда концентрации эстрогена в сыворотке крови превышали концентрации прогестерона. Именно эстроген, а не прогестерон способствует колонизации и инфицированию влагалища грибами рода кандида.<br />
Предполагается, что изменения в составе микрофлоры влагалища могут возникать под действием половых гормонов, или, по крайней мере, при изменении соотношения уровней эстрогена и прогестерона. Изменения в составе микрофлоры, по-видимому, происходят не одномоментно. Вероятно, существует определенная последовательность событий, при чем при размножении, например, Bacteroides spp. создаются благоприятные условия для размножения других бактерий, в частности М. hominis, микроорганизма, замыкающего эту цепь.<br />
У некоторых женщин дисбаланс в составе микрофлоры влагалища возникает под действием гормональных факторов. Поддержание этого состояния достигается с помощью каких-то дополнительных факторов. Следует учитывать возможную роль применения антибиотиков в возникновении и/или сохранения бактериального вагиноза. Прием антибиотиков способствует избыточному росту видов кандида. Дисбаланс в составе микрофлоры с последующим развитием бактериального вагиноза может быть вызван попаданием во влагалище экзогенных бактерий. Однако попадание во влагалище экзогенных бактерий у большинства женщин не служит пусковым механизмом развития бактериального вагиноза, а имеет значение размножение уже присутствовавших в нем ранее эндогенных микроорганизмов.<br />
Признание эндогенной природы бактериального вагиноза и ведущей роли гормональных факторов в его развитии не согласуется с точкой зрения о передаче этого заболевания половым путем. В то же время известно, что некоторые бактерии, ассоциированные с бактериальным вагинозом, могут передаваться при половых контактах от мужчины к женщине и наоборот.</p>

	        	
		
