<p><span class="bold">Неспецифический бактериальный вагиноз</span> характеризуется значительными изменениями влагалищной флоры и влагалищной среды. Последствием уменьшения или отсутствия физиологических лактобацилл, является недостаточное количество молочной кислоты и повышение рН влагалищной среды. Создавшаяся таким образом нейтральная среда благоприятствует развитию различных патогенных микробов, некоторых анаэробов, а также трихомонад.<br />
Считается, что грамотрицательные кокки лактобацилл, появляющиеся при влагалищных инфекциях, влияют на развитие патогенных микробов, в частности Gardnerella vaginalis. Грамотрицательные кокки почти всегда встречаются во влагалищном выделении с 5-ой степенью чистоты (трихомиониаз), а также с 3-ей степенью чистоты (гнойный бактериальный кольпит) (табл.7.1.).<br />
<br />
<span class="bold">Таблица 7.1.<br />
СТЕПЕНИ ЧИСТОТЫ ВЛАГАЛИЩА (Jirovec О. et d.)</span><br />
1. Здоровая нормальная флора Дедерлейна с палочкообразными лактобациллами.<br />
2. Негнойный бактериальный кольпит.<br />
3. Гнойный бактериальный кольпит.<br />
4. Гонококковая инфекция.<br />
5. Трихомонадная инфекция.<br />
6. Молочница (инфекция Candida albicans).<br />
<br />
Однако данное подразделение не лишено недостатков и является по своей сути классификацией вагинитов различной этиологии. В настоящее время разработана оригинальная классификация микроскопической характеристики биоценоза влагалища. В ней отражены четыре варианта биоценоза влагалища, микроскопические признаки каждого из них и соответствующие нозологические формы (табл. 7.2.).<br />
<br />
Влагалище - это динамическая экосистема, стерильная при рождении, которая начинает колонизироваться в течение нескольких дней преимущественно грамположительной флорой, состоящей из анаэробных бактерий, стафилококков, стрептококков, дифтероидов. До начала менструации рН влагалища близок к нейтральному - 7,0. В этот момент под влиянием эстрогенов вагинальный эпителий увеличивается до 25 слоев клеток, возрастает уровень гликогена в эпителии и влагалищная флора начинает меняться с преобладанием лактобацилл, а рН снижается менее 4,5. Эта величина сохраняется до наступления менопаузы, когда снижается продукция эстрогенов, вагинальный эпителий утоньшается и вновь восстанавливается смешанная флора анаэробных бактерий, стафилококков, стрептококков, дифтероидов, а рН влагалища повышается до 6,0.<br />
Лактобациллы влагалища, образующие молочную кислоту, выполняют защитную роль в отношении патогенных бактерий. При половом созревании во влагалище появляется гликоген, который является идеальным субстратом для роста лактобацилл. Лактобациллы превращают гликоген в молочную кислоту, понижают рН влагалища до несовместимого для других микроорганизмов уровня существования. При бактериальном вагинозе появляются влагалищные выделения, которые состоят из смешанной флоры (пептококки, пептострептококки, бактероиды, гарднереллы, мобилункус, микоплазма гоминис и небольшое количество вагинального эпителия).<br />
<br />
Микрофлора влагалища находится под постоянным воздействием организма, а также подвержена некоторым факторам внешней среды. Развитию бактериального вагиноза могут способствовать различные эндогенные и экзогенные факторы:<br />
<span class="bold">Эндогенные факторы:</span><br />
- изменение гормонального статуса;<br />
- снижение иммунологической реактивности;<br />
- нарушение микробиоценоза кишечника;<br />
<span class="bold">Экзогенные факторы:</span><br />
- предшествующая антибактериальная терапия;<br />
- перенесенные и сопутствующие воспалительные заболевания мочеполового тракта;<br />
- применение гормональных средств, иммунодепрессантов.<br />
